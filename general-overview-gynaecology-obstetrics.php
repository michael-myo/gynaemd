						<p>The field of medicine and surgical procedures that involves female health and pregnancy is broadly classified into&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_general.html">Gynaecology</a>&nbsp;and&nbsp;<a href="<?php echo homepage_url() ?>obstetrics_pregnancy.html">Obstetrics</a>. They deal with ailments relating to female reproduction, female organs and other pregnancy related issues.</p>
						<p><strong>The common conditions covered under gynaecology include:</strong></p>
						<ul>
							<li>Abortions</li>
							<li>Adenomyosis (growth of endometrial tissue into the muscular layer of the uterus)</li>
							<li>Bacterial Vaginosis and Lower Genital Tract Infections</li>
							<li>Cervical HPV Infections, Pre-cancer (CIN) and Cancer</li>
							<li>Benign lesions of Vulva</li>
							<li><a href="<?php echo homepage_url() ?>gynaecology_contraception.html">Contraception</a>&nbsp;(birth control)</li>
							<li>Dysmenorrhea (painful menses)</li>
							<li>Early pregnancy loss (Miscarriage)</li>
							<li>Ectopic Pregnancy</li>
							<li>Endometriosis (growth of endometrial tissue outside uterus)</li>
							<li>Fibroids</li>
							<li>Hormonal Therapy</li>
							<li><a href="<?php echo homepage_url() ?>gynaecology_infertility.html">Infertility Investigation and Treatment</a>&nbsp;(SOIUI,&nbsp;<a href="http://ivf.gynaemd.com.sg/" target="_blank" rel="noopener noreferrer">IVF</a>)</li>
							<li>Irregular Menses</li>
							<li><a href="<?php echo homepage_url() ?>gynaecology_menopause.html">Menopause</a>&nbsp;and related disorders</li>
							<li>Menorrhagia (heavy menses)</li>
							<li>Ovarian Cysts (Benign or Malignant)</li>
							<li>Premenstrual Syndrome (PMS)</li>
							<li>Prolapse (uterine, bladder, rectum, vaginal)</li>
							<li><a href="<?php echo homepage_url() ?>gynaecology_urogynaecology.html">Urinary Incontinence</a>&nbsp;(stress urinary incontinence, urge incontinence)</li>
							<li>Uterine Cancer</li>
							<li>Vaginitis (inflammation of the vagina)</li>
							<li>Vaginal Cancer</li>
							<li>Vulva Cancer</li>
							<li>Vulvovaginitis (inflammation of the vulva and the vagina)</li>
						</ul>
						<p><strong>Obstetrics on the other hand deals with:</strong></p>
						<ul>
							<li>3D, 4D and Detailed Fetal Anomaly Ultrasound Scanning</li>
							<li>Antenatal Risk Assessment for Chromosomal Abnormalities with OSCAR (One-stop Clinic for Assessment of Risk) and NIPT (non-invasive prenatal test)</li>
							<li>Antenatal Diagnosis (Amniocentesis, CVS) and Counselling</li>
							<li>Birth Plans and Delivery (Normal Vaginal, Vacuum, Forceps and Caesarean Section)</li>
							<li>Fetal Growth (Small for Gestational Age, Growth Retardation) and Wellbeing Assessment</li>
							<li>Fetal Malpresentation (Breech, Transverse and Oblique Lie)</li>
							<li>Multiple Pregnancy (Twins, Triplets)</li>
							<li>Pregnancy related issues (Hyperemesis Gravidarum, Gestational Diabetes, Preterm Labour, Preeclampsia)</li>
							<li>Post Partum issues (Breast feeding, Mastitis)</li>
						</ul>
						<p><strong>Courses and the education process to choose the profession</strong></p>
						<ul>
							<li>An&nbsp;<a href="<?php echo homepage_url() ?>">obstetrician and gynaecologist</a>&nbsp;has to first graduate with a basic medical degree (MBBS) in order to qualify as a doctor. After this basic medical degree, if they decide that they would like to pursue a career in obstetrics and gynaecology, they then have to sit for further exams (MRCOG, MRANZCOG, MMed, FAMS) before they can be regarded as a specialist in this field.</li>
						</ul>
						<ul>
							<li>After attaining these specialist qualifications, they may choose to go into one of the subspecialty fields instead of being a generalist. These include gynecological oncology, urogynaecology, reproductive endocrinology and infertility, and maternal-fetal medicine.</li>
						</ul>
						<ul>
							<li>As part of their rigorous training, aspiring obstetrician and gynaecologists have to undergo several years of internship in the various teaching hospitals under the direct supervision of experienced mentors to determine who has it in them to make the grade.</li>
						</ul>
						<ul>
							<li>Being a good doctor is not only about having in-depth medical knowledge. Being a great obstetrician and gynaecologist entails having excellent bedside manners, being sincere, having a genuinely caring disposition and having the ability to empathise and understand patients.</li>
						</ul>
						<p>So, if you are pregnant or have any gynaecological problems, be sure to consult a good gynaecologist who can attend to your needs. An excellent gynaecologist will make you feel comfortable shortly into your first consultation, and will take the time to answer your questions and address your concerns directly and sensitively so much so that your fears are allayed. A good gynaecologist should be able to answer your questions concisely. If he or she fulfils all these criteria, then that’s the one for you. If not, you should find another gynaecologist who would be more suited to your expectations.</p>

