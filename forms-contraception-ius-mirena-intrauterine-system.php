					<div class="page-content">

						<p>The IUS is quite different from the IUCD although it may look similar, because it contains a hormone. In addition to being a contraceptive, it actually ‘treats’ periods – usually making them shorter, lighter and pain-free. So it’s often used in order to treat period problems. It is T-shaped and inserted into the womb. It has a couple of short threads hanging from the foot of the ‘T.’ These are useful when the time comes to remove it. The stem of the ‘T’ contains a reservoir full of female hormone, of the type known as a ‘progestogen.’ (levonorgestrel). This hormone is steadily released into the womb, day after day, which can be for up to five years.</p>

						<p>It is very effective – and better than the IUD. Its effectiveness rate is generally around 99 per cent. It works by making the lining of the womb thinner, so it’s less likely to accept a fertilised egg. It thickens the mucus in the cervix, making it more difficult for sperm to reach an egg and it may stops the ovaries releasing an egg (ovulation). Most women can, but it may be unsuitable if:</p>

						<ul>

							<li>already pregnant</li>

							<li>Have cancer of the womb or ovary</li>

							<li>Have breast cancer now or within the past five years</li>

							<li>Have any other problem with the womb or cervix</li>

							<li>Have an untreated sexually transmitted infection or pelvic infection</li>

							<li>Have migraines with aura</li>

							<li>Have active liver disease</li>

							<li>Currently have thrombosis, heart or circulatory disease</li>

							<li>Have unexplained bleeding from the vagina (for example, between periods or after sex)</li>

						</ul>

						<p>Advantages:</p>

						<ul>

							<li>It works for five years</li>

							<li>It doesn’t interrupt sex</li>

							<li>periods usually become much lighter, shorter and less painful, and they may stop completely after the first year of use, so the IUS is helpful if women have heavy, painful periods</li>

							<li>It can be used if breastfeeding</li>

							<li>normal <a href="https://www.gynaemd.com.sg/gynaecology_infertility.html" title="female fertility">female fertility</a> returns as soon as the IUS is removed</li>

							<li>It can be used by women who cannot use estrogens</li>

							<li>It’s not affected by other medicines</li>

						</ul>

						<p>Disadvantages:</p>

						<ul>

							<li>Some women get acne</li>

							<li>Some women report mood changes, headaches or breast tenderness</li>

							<li>Some women develop small fluid-filled cysts on their ovaries – these aren’t dangerous and don’t usually need treatment</li>

							<li>There’s a very small chance of infection in the first 20 days after the IUS has been put in</li>

							<li>The IUS may come out (explusion) or it may move (displacement), but this is most likely to happen shortly after it has been put in</li>

							<li>The IUS may go through the cervix or womb (perforate) when it is put in</li>

							<li>If women become pregnant, there’s a small increased risk of having an ectopic pregnancy (a pregnancy occurring outside the womb, normally in the fallopian tube), but this risk is less than in women using no&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_contraception.html">contraception</a></li>

						</ul>

