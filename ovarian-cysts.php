					<div class="page-content">
						<p>During a woman’s child-bearing years, the ovaries go through cyclical changes. The ovary is responsible for the production of hormones, which in turn triggers menstruation. One of the most common women’s concerns is&nbsp;ovarian cyst, which is a fluidfilled sac that arises from the ovary. There are several types of cysts, most are harmless, asymptomatic and may ‘recover’ on their own.</p>
						<p>However, some women do experience one or more of the following symptoms when they are suffering from ovarian cyst:</p>
						<ul>
							<li>Fullness or abdominal distension</li>
							<li>Pelvic discomfort shortly after period begins or just before it ends</li>
							<li>Pelvic pain unrelated to menses that radiate to the back</li>
							<li>Deep pain during intercourse</li>
							<li>Discomfort or pressure during bowel movements</li>
							<li>Irregular or heavy menses</li>
							<li>Desire to urinate more frequently resulting from bladder compression</li>
						</ul>
						<p><strong>BENIGN CYSTS</strong></p>
						<p>Most cysts are not malignant. They will disappear at various times during the menstrual cycle and do not affect fertility. One of the examples is follicular cyst that develops when the follicle does not rupture and ovulate. It grows instead to form a cyst. They can be resolved within two to three cycles.</p>
						<p>There are cysts that are not related to the menstrual cycle such as endometriotic, dermoid and cystadenomas cysts. Endometriosis is a condition where the tissues that line the inside of the womb (endometrium) are found outside. They implant into the ovaries forming endometriotic cysts. These endometrial tissues continue to bleed with each menstrual cycle. The cyst may enlarge and rupture causing acute and severe pelvic pain.</p>
						<p>Dermoid cysts contain tissue such as hair, skin or teeth because they are formed from the cells that produce human eggs. Though rarely cancerous, they can become large causing painful twisting of the ovary, known as ovarian torsion. Both endometriotic and dermoid cysts tend to occur in younger women. Cystadenomas develop from ovarian tissue and sometimes may become cancerous with time and age.</p>
						<p><strong>THE LURKING DANGER</strong></p>
						<p>Though common cases of ovarian cysts are benign, this does not rule out the possibility of cancerous cysts. Ovarian cancer is the fourth most common cancer among women. It is also a leading cause of cancer death as it does not always result in symptoms until cancer has spread extensively. Cancerous cysts tend to occur in older women after menopause, though it can happen to woman of any age. Some of the contributing factors include inherited gene mutation, positive family history, never been pregnant before and history of breast and colon cancer</p>
						<p>Though there are no significant symptoms, there are some possible early signs of ovarian cancer. They are:</p>
						<ul>
							<li>Bloating</li>
							<li>Pelvic or abdominal pain</li>
							<li>Urgent or frequent urination</li>
							<li>Eating difficultly or feeling full easily</li>
						</ul>
						<p>It is important to note that the above symptoms do not necessarily mean a woman has ovarian cancer. However, women with a family history of ovarian, breast or colon cancer, above 50 years old, who has never given birth or is currently obese may have a higher risk of ovarian cancer.</p>
						<p><strong>TREATMENTS</strong></p>
						<p>When a patient is diagnosed with benign cysts, a gynaecologist has to advise a suitable treatment according to the nature of the cysts. Oral analgesia can be used to reduce the pain but they do not remove the cysts. Hormonal treatment in the form of oral contraceptive pills prevents ovulation which in turn reduces the chance of developing new functional cysts and ovarian cancer.</p>
						<p>Surgery is recommended if the symptoms are severe or getting worse, if the cyst is large and not deemed to be functional and if there is a suspicion of it being cancerous. The cyst can be removed without removing the ovary (cystectomy) especially if the cyst is benign and fertility is to be preserved. On the other hand, the cyst and ovary can be removed (oophorectomy) together if it is cancerous or if the patient is nearing menopause or already menopause as the risk of cancer increases in these older women.</p>
						<p>Both operations can be performed via laparoscopy (key-hole surgery) which requires more surgical skills but is more beneficial to the patient in terms of faster recovery, less discomfort and not to mention aesthetically more pleasing. Laparotomy (open surgery) is recommended if the cyst is cancerous or is too big or too adherent to be safely removed laparoscopically. There is no way to prevent ovarian cysts and there is always a possibility of recurrence after surgery. Regular check-ups and ultrasound scans with a trusted&nbsp;<a href="<?php echo homepage_url() ?>">Singapore gynaecologist</a>&nbsp;would better assure your health.</p>
