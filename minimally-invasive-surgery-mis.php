					<div class="page-content">
						<p><strong>What is Minimally Invasive Surgery (MIS)?</strong></p>
						<p>Minimally Invasive Surgery (MIS) commonly known as&nbsp; ‘<a href="<?php echo homepage_url() ?>gynaecology_mis.html">keyhole surgery</a>’ (laparoscopy or hysteroscopy) involves using small incision surgical techniques that are intended to&nbsp; achieve clinical outcomes akin to those of conventional open surgery but are associated with minimal discomfort, faster recovery time and shorter hospital stay.</p>
						<p>MIS uses skilled surgical techniques and special equipment and instruments to successfully perform the surgery.</p>
						<p><strong>Benefits of Minimally Invasive Surgery</strong></p>
						<p>Compared to traditional open surgery, MIS has the following benefits for patients:</p>
						<ul type="disc">
							<li>Less discomfort</li>
							<li>Less blood loss</li>
							<li>Smaller and more aesthetically acceptable surgical scars</li>
							<li>Less need for pain medication</li>
							<li>Earlier discharge from hospital</li>
							<li>Earlier return to normal activities</li>
						</ul>
						<p><strong>Limitations of Minimally Invasive Surgery</strong></p>
						<p>The Minimally Invasive Surgery is not suitable for all patients. It has certain limitations which include:</p>
						<ul type="disc">
							<li>May take more time to perform as compared to open surgery</li>
							<li>May cost more due to specialized equipment used</li>
							<li>Requires expert training and more skill so it may not be offered in some centres</li>
							<li>As a result of intraoperative complications, unexpected findings or safety concerns the minimally invasive surgery be converted into open surgery.</li>
						</ul>
						<p><strong>Treatment Options</strong></p>
						<p>Laparoscopic surgery incorporates the use of a thin tube-like instrument typically known as a laparoscope attached to a light source to visualize the area of concern inside the abdomen or plevis.</p>
						<p>A laparoscope is passed into the abdomen through a tiny incision (cut) in the skin. Laparoscopy is performed in order to determine the cause of abdominal/ pelvic pain or distention It can confirm or refute abnormal radiological findings (ultrasound scan, CT scans MRI, X-rays).</p>
						<p>Laparoscopic surgery can successfully remove endometriosis, pelvic adhesions, fibroids, uterus ovarian cysts and ectopic pregnancies.</p>
						<p><strong>How is it performed?</strong></p>
						<p>Laparoscopic surgery is performed under general anaesthesia. The skin over the abdomen is cleaned. The gynaecologist then makes a incision (cut) about 1-2 cm long into the navel (belly button).</p>
						<p>The abdomen is distended with CO2 gas. This allows the <a href="http://www.gynaemd.com.sg/" title="gynaecologist">gynaecologist</a> to visualise the internal organs clearly with the laparoscope which is inserted through the incision into the abdominal cavity. The laparoscope is connected to a monitor to allow the gynaecologist and the rest of the surgical team to visualize and perform the entire operation.</p>
						<p>Once the surgery is completed, the laparoscope and other instruments are removed. The incisions are closed and dressings are applied.</p>
						<p><strong>Recovery</strong></p>
						<p>The recovery time varies depending on the type of surgery that was performed. The discomfort post-surgery is usually minimal when compared to open surgery.</p>
						<p><strong>Laparoscopy risk factors</strong></p>
						<p>In laparoscopic surgery, the risk of complications may increase, depending on the complexity of the operation performed.</p>
						<p>In general, laparoscopic surgery is safe but as with all types of surgery, there are risk and complications involved.</p>
						<p>These include:</p>
						<ul type="disc">
							<li>Excessive bleeding requiring blood transfusion</li>
							<li>Inadvertent damage to abdominal structures, such as the intestines, bladder, blood vessels and nerves. This risk is low but if it does unfortunately occur, these can be surgically repaired.</li>
							<li>General anaesthesia risks requiring ICU care</li>
							<li>Pelvic and wound infections requiring a course of antibiotics</li>
						</ul>
