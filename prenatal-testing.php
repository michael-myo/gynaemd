						<p>Antenatal risk assessment tests help parents take an important step towards a healthy and happy&nbsp;<a href="<?php echo homepage_url() ?>obstetrics_pregnancy.html">pregnancy</a>.</p>
						<p>Becoming a parent is a major commitment filled with many challenges, rewards and decisions. While most pregnancies are uneventful and babies are born healthy, it is normal for couples to wonder if their baby will have chromosomal or other health problems or complications.</p>
						<p>Antenatal screening rests are designed to assess the risk of cerrain generic disorders or birth defects in individual pregnancies. Identifying whether the foetus is likely to have certain conditions can help couples make informed decisions on how to move forward.</p>
						<p>An example is the screening for Down syndrome. This is the commonest chromosomal abnormality, where the risk increases proportionately with increasing maternal age. As the aim of the rest is to identify women who are at high risk of having a Down syndrome baby, all women regardless of age are offered screening. The following are common types of screening:</p>
						<p><strong>NON-INVASIVE TESTING</strong>
							<br> These help to determine if a baby is at high or low risk of having a chromosomal abnormality. Only women who receive a high-risk result will be offered confirmatory diagnostic invasive resting.</p>
						<p><strong>OSCAR</strong>
							<br> The commonest test, OSCAR first trimester screening, involves a blood rest for pregnancy-associated plasma protein-A (PAPPA) and human chorionic gonadotropin (HCG). This is combined with an ultrasound scan to measure the space at the back of the baby’s neck (nuchal translucency) and is performed at between 11-13 weeks and 6 days.</p>
						<p>The results are matched to the mother’s age to determine the risk of having a baby with Down, Edwards and Parau syndromes. If the risk level is low, first trimesrer screening can provide much relief and offer the reassurance of a healthy pregnancy.</p>
						<p><strong>Non-Invasive Prenatal Test (NIPT)</strong>
							<br> NIPT is a group of newer tests that analyse the DNA of the baby via a simple blood rest on rhe mother, and includes panorama test, maternity 21 and harmony rest. NIPT is more sensitive and specific at screening for Down, Edwards and Parau syndromes; but they also cost more. Tests like panorama even screen for triploidy,
							<br> microdeletions, DiGeorge syndrome, Angelman syndrome, Cri-du-chat syndrome, Parder-Willi syndrome and certain sex chromosome abnormalities. The sex of the baby can also be determined.</p>
						<p>NIPT is possible from nine weeks as some of the baby’s DNA would have crossed into the mother’s bloodstream by then. Results take 7-14 days.</p>
						<p><strong>INVASIVE TESTING</strong>
							<br> Although The risks are low, invasive screening does present the risk of miscarriage, and is carried our only as a diagnostic confirmatory test.</p>
						<p><strong>Amniocentesis</strong>
							<br> This procedure draws a small amount of the amniotic fluid surrounding the baby by passing a fine needle through the mother’s abdomen and into the womb. Performed under ultrasound guidance, it’s usually safe from weeks 16-20 of pregnancy.</p>
						<p>About 0.2-0.3% of women who undergo amniocentesis from the 15th week of pregnancy could miscarry from the procedure. Results take from a few days to up to four weeks and if a disorder is diagnosed, generic counselling is offered.</p>
						<p><strong>Chorionic Villus Sampling (CVS)</strong>
							<br> In CVS, cells are taken from the placenra either through a transabdominal or a transcervical needle inserted into the uterus under direct ultrasound guidance. It can be performed as early as 11-12 weeks into the pregnancy, so parents get to know the baby’s condition sooner. The miscarriage rate of CVS is, however, higher, at 0.5-1%.</p>
						<p>Results take from a few days to 10 days. Patients with confirmed abnormal chromosomal results are referred for genetic counselling to help them make a decision as to whether to continue with the pregnancy.</p>
