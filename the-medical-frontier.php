					    <p>O&amp;G is a happy profession – there are lots of pictures of happy babies on my “wall of fame It’s different from other disciplines where you may have to break bad news. But you are also faced with tough challenges when you see how difficult it is for couples to conceive and that makes you really treasure kids and focus on the health of the baby, rather than its gender.</p>

					    <p>When you see the complications that can happen, you realise that as long as the child is normal, you’re just happy to have him or her.</p>

					    <p>Once you get past his youthful mien that belies his age, what does stand out about UK and Singapore-trained obstetrician &amp;&nbsp;<a href="<?php echo homepage_url() ?>">gynaecologist</a>, Dr Christopher Ng, is really his amiable demeanour. He puts you at ease at once, chatting to you like an old friend which is undoubtedly a skill that he successfully employs to make his patients feel more comfortable when they come to see him.</p>

					    <p>A graduate from Imperial College in London, he enjoys&nbsp;<a href="<?php echo homepage_url() ?>obstetrics_pregnancy.html">Obstetrics</a>&nbsp;and&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_general.html">Gynaecology</a>&nbsp;, in part because it’s a discipline that suits his personality. “If you’re a people person, enjoy having a good rapport with the patients, believe in the importance of excellent bedside manners – then you’ll probably choose the field of O&amp;G because you will need to talk a lot to your patients,” he says. But one great attraction is “Continuity”, adds Dr Ng. “As a gynaecologist you see your patients from young to their maturing years. Your patients grow with you, and hopefully with your clinic practice. You’re there with them for life… Women hardly change their gynaecologists once they have found one that they like. Patients may start off seeing me for pregnancy but after delivery, they continue to see me for routine gynaecology checkups. Conversely, patients may see me for a gynaecological problem like infertility and then if treatment is successful, they become my obstetric patient.”</p>

					    <p>Nor does he see that being a man puts him at a disadvantage, and in fact feels that it gives him a slight edge. “Because you’re conscious that you’re not a woman, it makes you listen that much harder to try to understand a woman’s problem better. You need to be more sensitive and put in more effort and care; in the space of five minutes, you need to get her to be comfortable enough to tell you all her problems and to allow you to examine her properly,” he explains.</p>

					    <p>There’s a lot of talk-time in the field of O&amp;G he says, and counseling is an important part of the job too. Like the consultations with women who see him because they have infertility problems. “A thirty per cent chance of conceiving with&nbsp;<a href="http://ivf.gynaemd.com.sg/" target="_blank" rel="noopener noreferrer">IVF</a>&nbsp;may not seem favourable, but for a person who has little hope otherwise of having a baby naturally and wants a child desperately, that is a big help,” he explains. “The physical and financial toll of going through an infertility program ultimately is still less than the emotional toll that a couple experiences especially if they are unsuccessful. But the sacrifice is well worth it when they get pregnant for both parents and doctor,” he adds. That is the rewarding bit about his field, because it tends to lead to happy endings. “O&amp;G is a field that relies too on word of mouth. If she’s comfortable with you, she tells everyone and becomes your best advertisement. You end up looking after their entire family and friends”. That appeals to him a lot, because it is a longterm thing.</p>

					    <p>Christopher hails from a family of doctors, three generations to be precise – his late grandfather was the director of medical services; his father is a professor of gynaecology at Singapore General Hospital, while his mother is a general practitioner. Growing up, he remembers sitting in the car, going to KK Hospital because his father had to attend to medical emergencies– “that gets you used to the idea that your time is not your own if you choose to pursue a career in O&amp;G,” he says smilingly.</p>

					    <p>Dr Christopher’s wife is also a gynaecologist and being in the same field, he says, does make it easier for us to understand why we have to run off to hospital in the middle of night when a delivery call happens. Sometimes, they both have had deliveries at the same time and they often wait for each other to finish. Married in 2008, they have an adorable daughter who is slightly over a year old now. Their family holidays are planned around their patients, although he notes that in private practice, there is more control to a certain extent. “I try to limit the number of deliveries I do a month so that each patient will have my undivided attention for that important day; it’s all about quality and personalised care in private practice rather than mass deliveries like a factory.“</p>

					    <p>Nowadays patients are more informed, “They would have done their reading or homework before they see me, and that reinforces what they think they might have,” he says. And because patients are more forthcoming, it also makes the doctor’s job less difficult – “it’s easier to extract information. You can tell them the options, and they can think about it, look it up on the Internet, and that opens up the platform for discussion, to decide what is the best treatment for her. It’s all about individual choices and treatment now, not a one-size-fits-all remedy,” he says, liking how knowledge gives women a choice and empowers them.</p>

					    <p>For him, the most challenging part of the job isn’t fielding questions, but “making all my patients’ wishes come through and managing those expectations. Many patients now not only want to get pregnant but also want to choose the sex of their baby (which is not allowed in Singapore). After managing many patients who suffer from <a href="https://www.gynaemd.com.sg/gynaecology_infertility.html" title="infertility">infertility</a> and seeing all that can go wrong, I often tell patients to be happy that they managed to get pregnant, had a successful delivery and that their baby is entirely normal and healthy.”</p>

					    <p>He notes that on the one hand, there are these breakthroughs that science and technology affords us – for example giving older women the ability to get pregnant where it was once near impossible before; but there needs to be a conscious effort not to play God and intervene too much, letting nature be as nature intended instead of say, selective implantations based on gender for example. He feels that such issues will come to the fore as people have fewer children, and ultimately, it’s a question of ethics versus Science, and trying to find that right balance between the two.</p>

