						<p><strong>Varicocele</strong>&nbsp;is a swelling of the veins that drain the testicle. This may prevent normal cooling of the testicle, leading to reduced sperm count and fewer moving sperm.</p>
						<p>Some&nbsp;<strong>infections</strong>&nbsp;(gonorrhoea, mumps) can interfere with sperm production or sperm health, or can cause scarring that blocks the passage of sperm.</p>
						<p><strong>Retrograde ejaculation</strong>&nbsp;(caused by diabetes, spinal injuries, medications, and surgery of the bladder, prostate or urethra) occurs when semen enters the bladder during orgasm instead of emerging out the tip of the penis.</p>
						<p><strong>Anti-sperm antibodies</strong>&nbsp;mistakenly identify sperm as harmful invaders and attack them.</p>
						<p><strong>Cancers</strong>&nbsp;and&nbsp;<strong>non-malignant tumours</strong>&nbsp;can affect the male reproductive organs or can affect the glands that release hormones related to reproduction. Surgery, radiation or chemotherapy to treat tumors can affect male fertility.</p>
						<p>Decreased fertility is more likely in men with&nbsp;<strong>undescended testicles</strong>&nbsp;which occurs during fetal development when one or both testicles fail to descend from the abdomen into the scrotum).</p>
						<p><strong>Hormone imbalances (low testosterone)</strong>&nbsp;resulting from disorders of the testicles or an abnormality affecting the hypothalamus, pituitary, thyroid and adrenal glands can cause infertility.</p>
						<p><strong>Sperm duct defects</strong>&nbsp;(absent or damaged)</p>
						<p><strong>Chromosome defects</strong>&nbsp;(Klinefelter’s syndrome) cause abnormal development of the male reproductive organs.</p>
						<p><strong>Sexual intercourse problems</strong>&nbsp;resulting from erectile dysfunction, premature ejaculation.</p>
						<p><strong>Medications</strong>&nbsp;like testosterone replacement therapy, long-term anabolic steroid use, chemotherapy can impair sperm production and decrease male fertility.</p>
						<p><strong>Previous surgeries</strong>&nbsp;(vasectomy, inguinal hernia repairs, scrotal or testicular surgeries, prostate surgeries)</p>
						<p><strong>Environmental element&nbsp;</strong>overexposure (heat, heavy metals, toxins, radiation/xrays and chemicals) can reduce sperm production or sperm function. Specific causes include:</p>
						<p><strong>Alcohol</strong>&nbsp;lowers testosterone and causes erectile dysfunction and decrease sperm production.</p>
						<p><strong>Tobacco smoking</strong>&nbsp;lowers sperm count.</p>
						<p><strong>Obesity</strong>&nbsp;causes hormone changes that reduce male fertility.</p>
						<p>The cause of male infertility is determined by performing scrotal ultrasound scan, hormonal tests and chromosomal analysis. Semen analysis measures the number of sperm present and looks for any abnormalities in the shape (morphology) and movement (motility) of the sperm. Sperm infections are screened. Several semen analysis may be done over a period of time to ensure accurate results.</p>
						<p>Treatment is directed at the cause:</p>
						<p><strong>Surgery</strong>&nbsp;can correct a varicocele or repair an obstructed vas deferens. Vasectomy can be reversed. In cases where no sperm are present in the ejaculate, sperm can be retrieved directly from the testicles or epididymis using sperm retrieval techniques as part of the&nbsp;<a href="http://ivf.gynaemd.com.sg/" target="_blank" rel="noopener noreferrer">IVF procedure</a>.</p>
						<p>Sperm infections should be treated with&nbsp;<strong>antibiotics</strong>.</p>
						<p><strong>Medication</strong>&nbsp;and&nbsp;<strong>counselling</strong>&nbsp;can help improve erectile dysfunction or premature ejaculation.</p>
						<p><strong>Hormonal treatments</strong>&nbsp;can use used as replacement therapy in cases while&nbsp;<strong>multivitamins</strong>(vitamin A, C, E, Zinc) and&nbsp;<strong>L-carnitine&nbsp;</strong>can be used to improve sperm quality and motility respectively.</p>
						<p>ART (assisted reproductive technology) treatments in the form of&nbsp;<strong>SOIUI</strong>&nbsp;(superovulation intracytoplasmic or&nbsp;<strong>IVF</strong>&nbsp;(in-vitro fertilization) /&nbsp;<strong>ICSI</strong>&nbsp;(intracytoplasmic sperm injection) may be needed in the end.</p>
