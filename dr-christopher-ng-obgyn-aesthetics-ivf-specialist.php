					    <p>As I was educated in Singapore and the United Kingdom, I like to think that I am an amalgamation of both cultures. I attended St. Andrew’s Primary during my early school days in Singapore. At the tender age of 13, my parents decided to send me to a typical English boarding school called Marlborough College in the UK for a more rounded education. As a result, I learnt at a very early age the survival lessons of how to be independent and to fend for myself in a faraway place without the “safety net and security” of having parents around. I already knew from the age of 6 that I wanted to become a doctor and this early desire to pursue a career as an&nbsp;<a href="<?php echo homepage_url() ?>">Obstetrician and Gynaecologist</a>&nbsp;stemmed from having two generations of prominent doctors in my family. My grandfather was the Director of Medical Services during Singapore’s colonial days and he was one of the first western trained Singapore doctors. My father is an Emeritus Professor of Obstetrics and Gynaecology and was former head of department and chairman of the Medical Board of Singapore General Hospital. My mother is a general practitioner in private practice. As a testament to their dedication to medicine, both my parents who are 75 years old, still currently work in their respective fields and have no plans to retire as they believe that working keeps one’s mind and body healthy. They (the pioneer generation) set a very good example and should inspire young Singaporeans of today that everyone can continue to contribute to society regardless of age. As an example that one is never too old to start a career in private practice, my father only started running a few sessions in our GynaeMD Clinic a few years ago.</p>
