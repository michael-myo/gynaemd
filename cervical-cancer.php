						<p align="justify"><strong>At what age should women start going for a pap smear? If they haven't had sex before, do they still need to go one? If so, what is the recommended age?</strong></p>
						<p align="justify">Only sexually active women need a pap smear test and they should start from the age of 25 onwards in Singapore (it is 21 years old in western countries). If a woman is sexually active for 3 years even if she is under 21 years of age, a pap smear is also recommended.</p>
						<p align="justify"><strong>How often should women go for a pap smear? Can the frequency decrease with age? At what age is it OK to stop?</strong></p>
						<p align="justify">The minimal standard is for pap smears to be repeated at least every 3 years until 65 provided there are no abnormalities.Routine pap smears are still the most common method for detecting cervical cancer. It accurately detects 90% of cervical cancers, even before symptoms develop. It can also be used to detect the presence of certain strains of human papillomavirus (HPV) that cause cervical cancer. Routine regular pap smears remain the gold standard for the early detection and diagnosis of HPV-associated <u>pre-cancerous and cancerous</u> lesions and this is universally accepted all round the world.</p>
						<p align="justify"><strong>What happens during a pap smear?</strong></p>
						<p align="justify">The purpose of a pap smear is to collect cells from your cervix, which is the lower end of your uterus. The pap smear can detect pre-cancerous changes early which can be removed before they turn cancerous. Cervical cancer screening involves routine Pap smears aimed at women with no symptoms.  It is a simple painless test in which cells from the cervix and vagina are examined for any abnormalities that could lead to cancer. A speculum is gently inserted into the vagina and opened so that the vaginal walls are spread apart enabling the gynaecologist to visualize the cervix. A brush is then used to brush the surface of the cervix to obtain cervical cells. The brush is then spread onto a slide (<u>pap smear</u>) or mixed in a liquid fixative (liquid-based cytology) and sent to a lab for examination under a microscope.</p>
						<p align="justify"><strong>What are the stats for cervical cancer in Singapore? Is it common among women aged 21-29? What about in the older age range?</strong></p>
						
						<p align="justify">Cervical cancer has dropped from being the 4th most common cancer in the 1970s to its current 10th commonest cancer affecting women in Singapore in 2016 according to the Singapore cancer registry. Between 2011-2015, cervical cancer accounted for 3.1% of all female cancers in Singapore. A total of 1,037 new cases of cervical cancer were diagnosed in the period 2011 - 2015. The age-standardised incidence rate of newly diagnosed cervical cancer in females has been decreasing since 1976. It has dropped by more than half from 17.3 per 100,000 person-years in the period 1976 - 1980 to 7.1 per 100,000 person-years in the period 2011 - 2015. Cervical cancer is not common in women 21-29 year old although not unheard of. It is more common to detect it in women between 35-55 years old.</p>
												
						<p><strong>Is it true that cervical cancer is usually caused by the HPV virus, which is sexually transmitted? If so, is a HPV test necessary for a woman who is sexually active?</strong></p>
						<p>Contrary to what many people think, cervical cancer is not a hereditary condition. In fact, the majority of cases are caused by a virus called Human Papillomavirus or HPV. HPV is a very common virus and it was found that in USA, up to 50% of couples who have ever been sexually active are likely to be infected with an HPV virus at some time in their lives. Routine testing for HPV is not necessary for all sexually active women. A pap smear will suffice for the majority of women. For certain women with the initial pap smear shows some abnormality, then testing for cancer causing HPV is useful to help determine the next course of management.</p>
						<p><strong>Even if a woman undergoes a biopsy of the cervix to remove some abnormal cells, can the abnormalities return? How often should she go for a pap smear then? Is a HPV test necessary?</strong></p>
						<p>Yes, for women who have undergone minor surgery to remove these abnormal cells, it can still return as new HPV infections can occur since HPV infections are sexually transmitted. Following surgery, repeat pap smears should be performed 6 monthly until the pap smears are back to normal. Following which, women can then go back to the normal routine pap smear regime.</p>
						<p><strong>Is it true that the odds of getting cervical cancer decrease if a woman has given birth? And that the odds decrease with every child she gives birth to?</strong></p>
                        <p>Quite the contrary, women who have given birth to 3 or more deliveries are at an increased risk of developing cervical cancer for reasons that are unknown at the moment. Some of the reasons postulated could be related to the fact that they have more unprotected intercourse to get pregnant (so increase risk of exposure to HPV infections) or that pregnancy hormones may make women more susceptible to HPV infections and cancer development. The immune system may be affected in pregnant women making them more likely to develop HPV infections and cervical cancer.</p>
                        <p><strong>Can cervical cancer be prevented?</strong></p>
                        <p>Yes, regular pap smears can pick up the precancerous stage and surgical treatment can then be done to remove these abnormal cells before they turn cancerous. There are also cervical cancer vaccines Gardasil@9 and Cervarix that can do one better by preventing the HPV infections and therefore cervical precancer and cancer. In the case of Gardasil@9, it protects against 90% of HPV cancer causing infections and Cervarix protects against 70%.</p>
