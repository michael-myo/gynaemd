						<p>It comes in the form of a self-adhesive patch which is stuck on a clean, dry, non-hairy area of skin (and not on breasts). Each patch lasts a week and needs to be changed every seven days. After using Evra for three weeks, there is a week’s break – during which time, the period arrives. So it’s ‘three weeks on, and one week off’ – just like the Pill. It stops the ovaries from releasing an egg (ovulation) each month. The patch also thickens the mucus in the cervix, making it difficult for sperm to reach an egg. It makes the lining of the womb thinner so it’s less likely to accept a fertilised egg.</p>
						<p>Women don’t have to remember to take a pill each day! But they do have to remember to put a new patch on every week – and people can forget to do this. The hormones are absorbed through the skin – not through the stomach. So there is very little nausea which the Pill often causes.&nbsp; Tummy upsets – like food poisoning will not interfere with its absorption unlike the Pill.</p>
						<p>The patch may come off – possibly without women noticing it. This happens in about 4% of women.Some women’s skins react badly to the patch. Skin irritation at the point of application makes about 2% of users give up. The patch doesn’t control periods (or period pain) in the way that the Pill does.</p>
						<p>The patch can have side-effects. The most common ones with Evra are breast discomfort, ‘spotting’ (slight blood loss from the vagina), headache, mild nausea and migraine. &nbsp;These side-effects tend to go away after women have been on the patch for a little while.</p>
						<p>A few women should not use Evra, including those with severe types of migraine and those with a tendency to thrombosis.
							<br> Failure rate is less than 1%, but it would be higher if women forgot to change patches. Pregnancy is more likely in bigger women.</p>
						<p>Advantages:</p>
						<ul>
							<li>only need to remember to replace the patch once a week</li>
							<li>It doesn’t interrupt sex</li>
							<li>Unlike the pill, the hormones do not need to be absorbed by the stomach, so the patch is not affected if you vomit or have diarrhoea</li>
							<li>Usually makes your bleeds regular, lighter and less painful</li>
							<li>It may help with premenstrual symptoms</li>
							<li>It may reduce the risk of cancer of the ovary, womb and colon</li>
							<li>It may reduce the risk of fibroids, ovarian cysts and non-cancerous breast disease</li>
						</ul>
						<p>Disadvantages:</p>
						<ul>
							<li>It’s visible</li>
							<li>It may cause skin irritation in a small number of women</li>
							<li>Like the pill, temporary side-effects at first may include headaches, nausea, breast tenderness and mood changes</li>
							<li>Breakthrough bleeding (unexpected bleeding while using the patch) and spotting can be common</li>
						</ul>
