					<div class="page-content">

						<p>Implanon is about the size of a thin matchstick. It contains a hormone called etonorgestrel. Most users have it inserted under the skin of their upper arm, not far above the elbow. The ‘match’ can easily be felt, but it’s not very visible – except to someone who is looking for it. There will be a tiny scar at the point where the device was inserted, but unless something has gone wrong, this shouldn’t be very visible either. Because of the local anaesthetic, there should be very little pain. Implanon is inserted during the first five days after the start of the period – mainly to ensure that the woman is not pregnant.</p>

						<p>Implanon lasts for three years, after which it runs out of hormone. It releases a steady stream of a female-type hormone into the bloodstream. The hormone reaches the ovaries, and prevents them from releasing eggs. It also causes some minor anti-conception changes in the womb lining and in the cervix. &nbsp;It requires a properly trained doctor to perform the insertion and removal. It is at least 99 per cent effective.</p>

						<p>Most women who want to use an implant can have one fitted. But it may be unsuitable if:</p>

						<ul>

							<li>already pregnant</li>

							<li>Do not want periods to change</li>

							<li>Take certain medicines</li>

							<li>Have active liver disease</li>

							<li>Have breast cancer now or within the past five years</li>

							<li>Have thrombosis, heart or circulatory disease</li>

							<li>Have migraines with aura</li>

						</ul>

						<p>Possible side-effects include although not common include:</p>

						<ul>

							<li><a href="http://www.netdoctor.co.uk/diseases/facts/headache.htm" target="_blank" rel="noopener noreferrer">headache</a></li>

							<li><a href="http://www.netdoctor.co.uk/menshealth/wellbeing/spots.htm" target="_blank" rel="noopener noreferrer">spotting</a></li>

							<li>weight gain</li>

							<li>breast tenderness</li>

							<li>dizziness</li>

							<li><a href="http://www.netdoctor.co.uk/diseases/facts/depression.htm" target="_blank" rel="noopener noreferrer">depression</a></li>

						</ul>

						<p>Advantages:</p>

						<ul>

							<li>Very effective</li>

							<li>Doesn’t interrupt sex</li>

							<li>Works for three years</li>

							<li>Can be used by women who cannot use estrogens or who are breastfeeding</li>

							<li><a href="https://www.gynaemd.com.sg/gynaecology_infertility.html" title="normal female fertility">normal female fertility</a> returns as soon as it’s removed</li>

							<li>May give some protection against cancer of the womb</li>

							<li>Offers some protection against pelvic inflammatory disease</li>

							<li>May reduce heavy, painful periods</li>

						</ul>

						<p>Disadvantages:</p>

						<ul>

							<li>Periods may become irregular, longer or stop</li>

							<li>Acne may occur</li>

							<li>Some women report mood changes and breast tenderness</li>

							<li>Requires a small procedure to fit and remove it and has to be performed by a trained doctor</li>

						</ul>

