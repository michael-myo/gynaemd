						<p><img class="alignnone size-full wp-image-654" src="<?php echo homepage_url() ?>dist/images/fertility-problem-520x245.png" alt="" width="520" height="245" srcset="<?php echo homepage_url() ?>dist/images/fertility-problem-520x245.png 520w, <?php echo homepage_url() ?>dist/images/fertility-problem-520x245-300x141.png 300w" sizes="(max-width: 520px) 100vw, 520px"></p>
						<p>With Singapore’s economic success, more and more couples are putting their career first before starting their family.</p>
						<p>Many are marrying later and starting their family at an older age compared to their parents’ generation. Statistically, the natural conception rate for a healthy young couple is 20 percent each month; this, however, drops to only 8 percent each month if a woman is in her early 40s.</p>
						<p>Similarly, success rates of&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_infertility.html">IVF</a>&nbsp;also decrease as women get older. Furthermore, there is also an increased risk of miscarriage and fetal chromosomal abnormalities associated with pregnant women above the age of 35. As such, it is advisable for women to try to complete their families before they reach 35 years old.</p>
						<p>For couples planning to start a family, having a healthy diet, exercising regularly, reducing stress levels and stopping smoking and drinking are some of the key advices worth heeding. Leading an unhealthy lifestyle and being overweight can cause many fertility problems. Excess body fat can lead to an overproduction of certain hormones that disrupt ovulation, leading to irregular menstrual cycles and decreased ovulation which can reduce a woman’s chances of getting pregnant.</p>
						<p>On the other hand, too little body fat is not ideal either as it can lead to the body producing inadequate amounts of hormones essential for ovulation and sustenance of a pregnancy in case of successful conception. Nutrients such as protein, iron, zinc and vitamin C have been shown to be important in fertility and deficiencies in these nutrients are linked to a higher risk of early miscarriage. A balanced and healthy diet is therefore essential not only for general health, but also for fertility.</p>
						<p>Lifestyle factors other than diet, such as smoking and alcohol consumption, can also have a great impact on both male and female fertility. Both cigarettes and alcohol have been shown to lower sperm production and motility, leading to difficulties in conception. The toxins found in cigarettes are known to not only damage a woman’s eggs, interfering with the fertilisation and implantation process, but also cause the ovaries to age. Alcohol consumption affects female fertility by lowering oestrogen levels. Besides, consumption of alcohol can have devastating effects on a developing fetus even if implantation was successful.</p>
						<p>Despite the many lifestyle modifications a couple can make to boost fertility, some people have genuine medical problems that make them unable to conceive. For women, fallopian tube obstruction and anovulation (not ovulating) are major causes of failed conception. Surgery can be considered to open up blocked fallopian tubes. It can also be used for several other conditions such as the removal of polyps, fibroids and endometriosis. Success of surgery ultimately depend on the type and extent of the problem.</p>
						<p>If a woman does not ovulate, medications such as clomiphene citrate may be helpful. This drug works by stimulating ovulation and most women treated this way eventually start to ovulate regularly. For simple cases of anovulation without compounding factors, the success rate of conception can be up to 50 percent within six treatment cycles. A range of other assisted reproductive technologies may also be helpful for couples facing a variety of fertility problems.</p>
						<p>Superovulation Intrauterine Insemination (SOIUI) is a technique that requires both fallopian tubes to be patent. Several follicles in the ovary are first induced to mature with oral or injectable medication and ultrasound scans are done regularly to determine the number and size of eggs within the ovary. When the time is right, a fresh sample of the husband’s semen will undergo special preparation in the lab before being placed directly into the womb with a fine tube. Hormones are then given to improve the chances of implantation and pregnancy. SOIUI is offered for mild male factory&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_infertility.html">infertility</a>, mild endometriosis, cervical factor infertility, unexplained infertility and after several failed clomiphene citrate cycles. Both tubes must be patent in order for SOIUI to be performed.</p>
						<p>For In Vitro Fertilisation (IVF), multiple eggs are stimulated to mature at the same time using higher doses of injectable hormones to mature at the same time and these are removed from the ovaries vaginally under ultrasound guidance and fertilisation then occurs outside the body in the lab. A few days later, one or two of the fertilised embryos are placed in the woman’s uterus while the unused embryos created by this technique can be frozen in storage for future use if needed. Since fertilisation occurs ex vivo, women with blocked or irreparably damaged fallopian tubes, severe endometriosis, ovulatory dysfunction, unexplained infertility as well as husbands with severe sperm disorders may benefit from this technique.</p>
						<p>Intracytoplasmic Sperm Injection (ICSI) is a technique similar to IVF except that a single sperm is injected directly into each individual egg to increase the chances of successful fertilisation. This technique is particularly useful when there is low sperm quality or quantity in the man because only a few healthy sperms are required for this to be carried out.</p>
						<p>Couples considering assisted reproductive technologies should arrange for a detailed consultation with a&nbsp;<a href="<?php echo homepage_url() ?>abt_us_doctor_writeup.html">gynaecologist</a>&nbsp;to discuss the suitability of the techniques based on each individual’s case. Besides helpful advice on treatment, a specialist can also provide invaluable emotional support to the couple when a fertility-related problem is discovered. By providing a balanced view on treatment options available, gynaecologists can allow patients to make informed decisions as to what is the best route to follow. Infertility is a very hard situation to deal with for most couples. Keeping the romantic relationship strong, talking about the issue with family, friends or a support group, and becoming informed about the condition and treatment options can all make this time easier to cope with.</p>
