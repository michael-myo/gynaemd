						<p>Uterine (womb) prolapse happens when the uterus descends down into the vagina and can sometimes even protrude out of it. This is a result of weakened pelvic floor muscles and ligaments which usually support the uterus and hold it within the pelvis. Although uterine prolapse can affect women of any age, it usually occurs in post menopausal women or women who have had many vaginal deliveries or difficult vaginal births. Aetiological factors include the effects of gravity, lack of oestrogen (as in menopausal women), excessive damage and straining onto the pelvic floor muscles and ligaments during childbirth.</p>

						<p>A common cause is the lack of muscle tone along with the accompanying laxity of the pelvic floor ligaments (support for the uterus) due to the decrease in oestrogen levels found in menopausal women. Another contributing cause of uterine prolapse is damage to the supporting ligaments and muscles of the pelvic floor as a result of prolonged labour or difficult vaginal deliveries. Other causes include increasing number of vaginal births, delivering big babies, recurrent straining (chronic constipation and coughing), heavy lifting and previous vaginal surgeries with poor tissue healing.</p>

						<p>The symptoms depend very much on the severity of the prolapse. Women with mild prolapse may be asymptomatic, while women with moderate to severe prolapse may start experiencing symptoms such as the feeling that something is “falling” out of the vagina, heaviness along with discomfort, urinary leakage or retention, backaches and abnormal bowel movements. In some severe cases, the prolapse may be seen partially or completely protruding out of the vagina.</p>

						<p>Besides the uncomfortable sensation of the uterus prolapsing out of the vagina, uterine prolapse may very often be accompanied with prolapse of other organs like the bladder and rectum. With bladder prolapse (cystocele), women may experience <a href="https://www.gynaemd.com.sg/gynaecology_urogynaecology.html" title="urinary stress incontinence">urinary stress incontinence</a>, difficulty passing urine and frequent urinary tract infections. In rectal prolapse (rectocele), women may complain of difficulty passing motion. In some cases of uterine prolapse, ulcers may develop as a result of friction between the prolapse uterus, vaginal tissue and underwear. This can then get infected.</p>

						<p>So what are the treatment options? In mild uterine prolapse with or without symptoms, regular pelvic floor exercises (Kegels) to strengthen pelvic support and muscles may be all that is needed. Avoid excessive straining, heavy lifting and reduce weight. These will help to alleviate pressure on the pelvic floor muscles and ligaments which can weaken over time. In severe cases, vaginal pessaries can be inserted into the vagina to prevent further prolapse. They provide relief to patients who are unfit for surgery and for those who decline surgery. Vaginal pessaries can sometimes lead to ulcers and infections as a result of incorrect fitting and poor hygiene respectively.</p>

						<p>Surgery is usually recommended by the <a href="http://www.gynaemd.com.sg/" title="gynaecologist">gynaecologist</a> for fit and mobile patients with severe prolapse or for those in which conservative treatments have failed to relieve their symptoms. The uterus is commonly removed vaginally and any loose vaginal skin is excised. The weakened and lax vaginal supports are then strengthened with strategically placed sutures. Post surgery, women are often advised to lose weight if they are overweight and to avoid smoking, excessive coughing, sneezing, straining and heavy lifting.</p>

