						<p>Each pregnancy is a unique experience and so couples are encouraged to be actively involved in making decisions about their antenatal care. This includes information on services that are available and support to help you arrive at your decision. During your&nbsp;<a href="<?php echo homepage_url() ?>obstetrics_pregnancy.html">pregnancy</a>&nbsp;you will be given a series of antenatal appointments to check on your health and the health of your baby.&nbsp; Becoming a parent is a major commitment filled with many challenges, rewards, and decisions. Making healthy choices during your antenatal visit is an important step to a healthy and happy pregnancy so setting aside ample time for the visit and going as a couple is always a good idea.</p>

						<p>Antenatal care is intended to check your health and to screen for anything that could affect your growing baby.</p>

						<p>A physical examination to determine your weight, height, blood pressure along with an ultrasound scan to confirm the estimated date of delivery of your baby is mandatory. The ultrasound scan is also performed to assess the viability of your pregnancy, to exclude ectopic pregnancy and multiple pregnancies (twins, triplets etc). In addition, routine blood tests to determine your blood type, including Rh factor (a specific protein on the surface of red blood cells) along with blood tests to screen for thalassemia and any exposure to syphilis, rubella, hepatitis B and HIV will be ordered. A sample of your urine is taken to screen for the presence of too much sugar or protein in your urine which may suggest diabetes or kidney disease. Depending on your medical history and risk factors, further investigations may need to be ordered.</p>

						<p><strong>Is it necessary to have antenatal screening for fetal anomaly?</strong></p>

						<p>Yes, you will be offered several tests non-invasive tests designed to screen for fetal abnormalities. Down syndrome is the commonest chromosomal abnormality and its risk increases with increasing maternal age.&nbsp; As the aim of antenatal risk assessment (<a href="https://www.gynaemd.com.sg/obstetrics_risk_assessment.html" title="pregnancy screening">pregnancy screening</a>) is to identify women at high risk of having a Down syndrome baby and to then offer them invasive diagnostic testing using chorionic villus sampling (if &lt; 13 weeks gestation) or amniocentesis (if &gt; 15 weeks gestation), all women regardless of age are offered screening. These non-invasive screening tests are designed to identify women in whom a risk of Down syndrome is sufficiently high to justify invasive testing and to minimise the risk of miscarrying a healthy baby.</p>

						<p>There are several tests available. The most commonly offered is the OSCAR first trimester screening which involves a blood test for pregnancy-associated plasma protein-A and human chorionic gonadotropin. This is combined with an ultrasound scan to measure the space at the back of the baby’s neck (nuchal translucency) and is performed between 11 to 13 weeks and 6 days. This measurement and blood tests are combined with the mother’s age to calculate the risk of having a baby with Down, Edwards and Patau syndromes. If the risk level is low, first trimester screening can be of much relief and offer reassurance of a healthy pregnancy.</p>

						<p>There are other newer tests called Non-Invasive Prenatal Test (NIPT) which are more sensitive and specific at screening for Down, Edwards and Patau syndromes but they cost more. They can even screen for certain sex chromosome abnormalities, triploidy, mircodeletions, DiGeorge syndrome, Angelman syndrome, Cri-du-chat syndrome and Parder-Willi syndrome. The sex of the baby can also be determined. The DNA of the baby can be detected by performing a simple blood test on the mother from 9 weeks into her pregnancy as some of the fetal DNA crosses into the mother’s blood stream. It takes 7-10 days for the results.</p>

						<p>Finally there is the detailed ultrasound scan to screen for any fetal structural abnormalities or deformities which may or may not suggest an abnormal genetic link.</p>

						<p><strong>Do I need a special diet for two?</strong></p>

						<p>Some women feel that since they are pregnant, they need to eat more in order to “feed” their foetus and sometimes they overdo it by over eating. Being pregnant doesn’t mean that women have to start eating for “two people”. Consuming prenatal vitamins along with a healthy well balanced diet is more than enough. Women should eat properly and not feel that they need to eat constantly in order to provide nutrition for their foetus. Have 3 proper meals and avoid over eating is usually recommended. Increase intake of foods high in fiber and low in fat (orange juice, leafy vegetables) and every diet should include proteins, carbohydrates, vitamins, minerals, and fat.</p>

						<p><strong>Why is folate important?</strong></p>

						<p>Folate is a B vitamin essential in the development of baby’s nervous system. It reduces the chance of having a baby with a neural tube defect (an abnormality disability affecting the nervous system). The recommended dose for all women is 400 mcg of folic acid&nbsp;daily at least one month before you conceive and for the first three months of your pregnancy.</p>

						<p><strong>Why do I feel bloated?</strong></p>

						<p>The bloated feeling during pregnancy especially after eating is the result of several contributory factors and most are a result of rising pregnancy hormones which cause delayed gastric (stomach) emptying and constipation. This may be accompanied with heartburn and indigestion. As the pregnancy progresses, the baby also increases in size which compresses onto the stomach and intestines. Women also retain more water and gain fat which all contribute to that bloated feeling. Try taking small more frequent meals and avoid spicy food, gassy drinks and citrus juice and fruits until the gastritis improves. Drink plenty of water and consume more fiber in your diet to ease bowel movement. Oedema or water retention is a common complaint that occurs towards the latter part of pregnancy resulting from standing for long periods. Regular exercise can help improve blood circulation and reduce venous congestion.</p>

						<p><strong>What are the common complaints of pregnancy?</strong></p>

						<p align="justify">Constipation, backache, feeling bloated, feeling faint, heartburn / indigestion, morning sickness, piles, breast tenderness, stretch marks / striae, thrush, urinary tract infections, frequency of urine and varicose veins are some of the common complaints during pregnancy.</p>

						<p align="justify">Most of these are caused by hormonal changes. They are usually temporary, cause minor discomfort and can be treated simply with medication or a change in lifestyle and daily routines.</p>

						<p align="justify"><strong>What are the symptoms of adverse events in pregnancy?</strong></p>

						<p align="justify">Pre-eclampsia, preterm labour, abnormal vaginal bleeding, reduced fetal movements and prelabour rupture of membranes are some of the adverse events in pregnancy. It is advisable to seek an urgent consultation with your obstetrician should you experience any of these symptoms.</p>

						<p align="justify"><strong>Can I smoke and drink alcohol during pregnancy?</strong></p>

						<p align="justify">Consuming alcohol during pregnancy effects the growing fetus which is why gynaecologists generally advise pregnant women not to drink alcohol at all. This is because your baby’s brain, heart and blood vessels begin to develop in the early weeks of pregnancy. The harmful effects really depend on the amount of alcohol consumed and the duration of consumption with the longer and more you drink during pregnancy, the greater the risk to your unborn fetus. These harmful effects of fetal alcohol syndrome include physical deformities, mental retardation, learning disorders, visual difficulties and behavioural problems which are irreversible. Some of these physical deformities may be detected with ultrasound scans. The best way to prevent damage is to avoid drinking any alcohol for the rest of your pregnancy and consuming your antenatal folate and multivitamins.</p>

						<p align="justify">Smoking can be harmful to you and your baby as carbon monoxide reduces the baby’s oxygen and nutrient supply. Nicotine exposure also reduces fetal breathing movements and increases baby’s heart rate. Smoking during pregnancy has been linked with abnormal vaginal bleeding, placental abruption (premature placental separation), placenta praevia (placenta total or partial covering of the cervix), premature rupture of the membranes, preterm labour, preterm birth, miscarriage, low birth weight babies and birth defects.</p>

						<p align="justify"><strong>Is breastfeeding important?</strong></p>

						<p>Breastfeeding is not only beneficial for your baby’s health and development but also for nurturing the emotional bond between mother and baby. Breast milk contains all the vital nutrients and antibodies that protects your baby during the early months. Breast milk contains the correct balance of nutrients for your baby and boosts your baby’s immune system.</p>

						<p>Moreover, breastfeeding is said to also reduce the chance of becoming prone to allergies, eczema or asthma, especially if there is a strong family history of these conditions.</p>

						<p><strong>What should I do about long-term medications?</strong></p>

						<p>You should discuss with your obstetrician before taking any medication especially during the first few months of pregnancy when the baby’s organs are developing. This is because certain drugs can affect the development of the baby. If you are on long-term medication for certain medical conditions, you should review them before you conceive so that the dose and the type of medication can be modified or reduced so as to give you the best chance of delivering a normal healthy baby for a baby.</p>

						<p><strong>Can I continue to exercise?</strong></p>

						<p>Regular exercise helps keep you fit during pregnancy and may improve your ability to cope with the pain of labour. It makes it easier for you to get back into shape after birth. Most forms of exercise are safe during pregnancy. Some less strenuous exercise like walking is a good and brisk walking provides a gentle workout without stressing your joints and muscles. This is especially important if you are not actively exercising before your pregnancy. Swimming is also recommended as the water supports your weight so avoiding muscle and joint strains. It also helps you stay cool and helps prevent your legs from swelling. Cycling although good can be more difficult as your growing belly enlarges making it difficult for you to keep your balance and avoid falling. Stationary cycling in the gym may be better especially in the later stages of pregnancy. Running or jogging is fine if women are doing this routinely before getting pregnant. Women should remember that because they are pregnant, they will need to reduce the distance they run or keep the same distance but run at a slower pace so as not to over strain themselves. Prenatal yoga is an excellent way to improve one’s posture, muscle tone and strength. It is a good method of relieving backaches and muscle strains during pregnancy. It also helps women to relax and to prepare for childbirth. Whatever the exercise you choose, always start off slowly and work at a pace you are comfortable with and remember to give yourself plenty of time to warm up and cool down before and after any exercise. Mild to moderate exercise is fine and will not increase your risk of miscarriage.</p>

						<p><strong>Is it safe to have sex?</strong></p>

						<p>It is generally safe to have intercourse when you are pregnant as this has not been shown to increase the chances of having a miscarriage. If you are however experiencing the possible signs of miscarriage, like pain or bleeding, then it is advisable not to engage in intercourse. It is best to change your sexual position to one that is most comfortable as your pregnancy advances. For example in early pregnancy, being in the traditional missionary position is possible but as your pregnancy progresses with your ever enlarging womb, you may find it more comfortable to be on top or to lie on your side. If you have suffered from any early bleeding, premature labour or have a low-lying placenta, it is advisable to consult your gynaecologist who may suggest that you abstain altogether.</p>

						<p><strong>Is there any workplace threats?</strong></p>

						<p>While most women can safely continue to work during pregnancy, it is advisable to keep away from substances like pesticides, insecticides and certain chemicals or&nbsp; do not get exposed to hydrocarbon solvents like dry-cleaning fluids, lead or mercury.</p>

						<p><strong>Can I travel during pregnant?</strong></p>

						<p>For most women, commercial air travel is safe during pregnancy. If possible, flying during mid-trimester (14 to 28 weeks) may be the best time as this is when the risks of miscarriage and premature labour are at their lowest. It is also advisable to check with your gynaecologist and the specific airlines as to their policies on flying before booking your flight (usually up until 35 completed weeks). On long haul flights (more than 4 hours), the risk of developing a deep vein thrombosis (blood clot) in the legs increase so it is a good practice to get up and walk about every 3-4 hours. Wearing compression stockings on board may also reduce the chances of developing a deep vein thrombosis.</p>



