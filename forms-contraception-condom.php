						<p>I will be sharing different forms of contraception in this and next few posts. As you know that there is no 1 ideal contraception that suits all women. Women have many reasons for seeking&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_contraception.html">contraception</a>&nbsp;and their final preference depends on the stage of their reproductive cycle as well as medical, social economic and religious factors.</p>
						<p><strong>Condom</strong></p>
						<p>Male condoms are sheathes that trap the sperm when a man climaxes (‘comes’). Wearing them greatly reduces the chances of pregnancy. They also provide some protection against sexually-transmitted infections (STIs), including&nbsp;<a href="http://www.netdoctor.co.uk/diseases/facts/hiv_aids.htm" target="_blank" rel="noopener noreferrer">HIV</a>. It is therefore an excellent means of contraception for young couples or for people with multiple sexual partners. &nbsp;But this protection is far from 100 per cent. A condom’s effectiveness largely depends on the person who uses it. If a man pulls it on roughly, lets his partner snag it with her teeth, or only puts it on halfway through intercourse, this will greatly reduce the protection it offers.</p>
						<p>When used correctly, a male condom is about 98 per cent effective. This means that only about 2 in every 100 women would get pregnant in the course of a year. This is more effective than several other forms of&nbsp;<a href="http://www.netdoctor.co.uk/sex_relationships/facts/contraception_which.htm" target="_blank" rel="noopener noreferrer">contraception</a>, such as&nbsp;<a href="http://www.netdoctor.co.uk/sex_relationships/facts/coitusinterruptus.htm" target="_blank" rel="noopener noreferrer">withdrawal</a>&nbsp;or using&nbsp;<a href="http://www.netdoctor.co.uk/sex_relationships/facts/contraceptivechemical.htm" target="_blank" rel="noopener noreferrer">spermicides (chemicals)</a>&nbsp;alone. Pregnancy may occur if couples don’t put the condom on before intercourse starts or if the condom splits or if couples use an oil-based lubricant, such as Vaseline, creams or lotions – this can make holes in latex condoms. A few men sometimes complain that condoms dull their pleasure during intercourse and effects their orgasm and so are not keen to use this method. some allergies to condoms have been reported but this is usually rare. Condoms are easily available off the shelf and don’t need a doctor’s prescription.</p>
						<p>The female condom (Femidom) was invented about 15 years ago. It looks like a tiny plastic bin liner. There is a ring round the opening and another one at the closed end – which is the end that goes into the top of the vagina.
							<br> It is made of polyurethane, and not latex. So it is unlikely to provoke allergies, and should not be damaged by oil-based lubricants. The female condom is pretty effective, as long as it is correctly inserted into the vagina – and provided the man doesn’t put his penis outside it.</p>
						<p>It is over 95 per cent effective. The woman (or her partner) puts the female condom inside her vagina before sex. Couples are not protected if you only put it in halfway through sex and risk getting pregnant. Many people don’t realise sperm can be present in the man’s pre-come fluid, which he secretes as he gets sexually excited. So putting the condom in after intercourse has started may already be too late. Care must be taken not to tear the condom with fingernails or rings.&nbsp; Although the female condom is still nowhere near as popular as the male one, some couples do like it. Some women are keen on the idea that it lets them control their own fertility especially if the male partner refuses to wear a male condom. They also provide some protection against&nbsp;<a href="http://www.netdoctor.co.uk/health_advice/facts/venerealdiseases.htm" target="_blank" rel="noopener noreferrer">sexually-transmitted infections</a>&nbsp;(STIs), including&nbsp;<a href="http://www.netdoctor.co.uk/diseases/facts/hiv_aids.htm" target="_blank" rel="noopener noreferrer">HIV</a>. As both types of condoms need to be inserted before sex, some couples feel that this distracts from the spontaneous act of intercourse.</p>
						<p><em>Advantages:</em></p>
						<ul>
							<li>Very effective</li>
							<li>Easily available (male condoms)</li>
							<li>Only need to use them when you have sex</li>
							<li>Help to protect against some sexually transmitted infections (STIs), including HIV</li>
							<li>Male condoms come in many different varieties, shapes and sizes</li>
							<li>Female condoms can be put in at any time before sex</li>
						</ul>
						<p><em>Disadvantages:</em></p>
						<ul>
							<li>Can interrupt sex</li>
							<li>Male condoms can slip off or split if used incorrectly</li>
							<li>When using the female condom care is needed to ensure the penis goes inside the condom and not down the side of the condom and the vagina</li>
							<li>Some people are sensitive to the chemicals in latex condoms, although this is not common</li>
							<li>Oil-based lubricants, such as body oils or lotions, should not be used with latex condoms</li>
						</ul>
