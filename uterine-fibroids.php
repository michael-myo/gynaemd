						<p>Thorough check-up should clear the air about growths in the uterus.</p>
						<p align="justify">Uterine fibroids are noncancerous growths of the uterus that often occur during a woman’s childbearing years. They are also called fibromyomas, leiomyomas or myomas and are not associated with an increased risk of uterine cancer and only 1% ever become cancerous. Uterine fibroids develop from the smooth muscular tissue of the uterus (myometrium).</p>
						<p align="justify">A single cell reproduces repeatedly, eventually creating a pale, firm, rubbery mass distinct from neighbouring tissue.</p>
						<p align="justify">A strong family history (mother, sister with fibroids) increases one’s risk of developing fibroids, as is race. Afro-Caribbean women have a higher risk and fibroids tend to occur at a younger age, develop more and are larger in this group of women.</p>
						<p align="justify">Three out of four women have uterine fibroids sometime during their lives, but most are unaware of their existence because they are often asymptomatic. They may be discovered incidentally during a pelvic examination or pelvic ultrasound scan. The most common symptoms of uterine fibroids include:</p>
						<ul>
							<li>Heavy menstrual bleeding (menorrhagia)</li>
							<li>Prolonged menstrual periods (lasting 7 or more days)</li>
							<li>Pelvic pressure or pain</li>
							<li>Frequent urination (frequency)</li>
							<li>Difficulty emptying your bladder (urinary retention)</li>
							<li>Constipation</li>
							<li>Backache</li>
						</ul>
						<p>Very rarely, a fibroid can outgrow its blood supply and begin to die, causing acute pain. A fibroid that hangs by a stalk outside the uterus (pedunculated fibroid) can twist on its stalk and cut off its blood supply leading to acute pain. Symptoms and signs are related to the location of the fibroid:</p>
						<ul>
							<li>Subserosal fibroids project to the outside of the uterus and sometimes press onto the bladder, causing urinary symptoms. If fibroids bulge from the back of the uterus, they occasionally can press either on the rectum, causing constipation, or on the spinal nerves, causing backache.</li>
							<li>Submucosal fibroids grow into the inner cavity of the uterus and are thought to be the main cause for prolonged, heavy menstrual bleeding and can be a problem for women attempting pregnancy (increasing the risk of miscarriage).</li>
						</ul>
						<p><strong>INVESTIGATION</strong></p>
						<p>Fibroids are commonly diagnosed on pelvic ultrasound scans. Computerised tomography (CT) and magnetic resonance imaging (MRI) can also be employed but are more costly. Hysterosonography uses sterile saline to expand the uterine cavity, making it easier to obtain interior images of the uterus with an ultrasound scan. This test may be employed if there is heavy menstrual bleeding despite normal results from traditional ultrasound.</p>
						<p>Hysterosalpingography uses a dye to highlight the uterine cavity and fallopian tubes on X-ray images. In addition to revealing fibroids, it can determine fallopian tube patency at the same time for women experiencing concurrent infertility. Hysteroscopy involves inserting a small, lighted telescope through the cervix into the uterus simultaneously distending the uterine cavity with saline thereby facilitating direct examination of the uterine cavity.</p>
						<p>For the majority of women who are asymptomatic, routine follow up ultrasound scans to detect for any increase in size or number is recommended as they are not cancerous, grow slowly and tend to shrink after&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_menopause.html">menopause</a>&nbsp;when levels of female hormones drop. Medications for uterine fibroids act on hormones that regulate menstrual cycle and treat the symptoms (heavy menstrual bleeding, pelvic pain). They do not remove fibroids, but may shrink them.</p>
						<p>Surgical treatment is recommended when fibroids are too big, too numerous, rapidly growing (potentially malignant) and when medical treatment fails.</p>
						<p>Myomectomy involves removing the fibroids but leaving the uterus behind. This is the treatment of choice if fertility is desired. There is a risk of fibroid recurrence.</p>
						<p>Hysterectomy (removal of the uterus) is the only permanent solution for uterine fibroids but patients will not be able to conceive after this. Myomectomy and hysterectomy can be performed abdominally (open method), laparoscopically (minimally invasive surgery) or vaginally.</p>
