						<p><strong>Diaphragm</strong></p>
						<p>The diaphragm is put in the vagina before sex, positioned so that it keeps sperm from getting anywhere near the cervix. Diaphragms are ‘domes,’ made of thin, soft rubber. They form an excellent physical ‘barrier’ to sperm and are use in conjunction with spermicide. A diaphragm without spermicidal cream will not be very effective in preventing pregnancy. Women’s vaginas come in various sizes (particularly after they’ve had children) so they need to have their vaginal size assessed by a doctor before using diaphragms. In addition they need to be taught how smear spermicide onto the diaphragm and how to insert it so that the cervix is covered. This makes the diaphragm less popular than the oral contraceptive pill (OCP) and condoms.</p>
						<p>Used properly, the diaphragm is an efficient and safe type of&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_contraception.html">contraception</a>. It has a success rate of roughly 92 to 96 per cent per year among women who’ve been trained to use it correctly. It is higher for women in their late 30s or 40s – which is when many women use it. But if women just ‘stick it in’ at the last moment, without being too sure where it’s going, then the success rate will be much lower. Each diaphragm will last several years if it is carefully looked after, so this is an inexpensive contraceptive method. Diaphragms can be used by most women without any kind of inconvenience or discomfort. So this is a good option for a woman who dislike&nbsp;<a href="http://www.netdoctor.co.uk/sex_relationships/facts/condoms.htm" target="_blank" rel="noopener noreferrer">condoms</a>, or who cannot take&nbsp;<a href="http://www.netdoctor.co.uk/sex_relationships/facts/contraceptivepills.htm" target="_blank" rel="noopener noreferrer">the Pill</a>&nbsp;for some reason or another, or for women who can’t use IUDs. It’s also popular with women who are older, and whose fertility is therefore a little lower.</p>
						<p>One of the key advantages to the diaphragm is that it can allow sex to be spontaneous. A woman can easily insert the diaphragm whenever she expects she may be having sex that day. If inserted for more than three hours before intercourse extra spermicide must be used. Diaphragms should always be left in place for at least six hours after sex. It should not be left inside for more than 30 hours, as this might possibly cause an infection and/or a discharge. Another advantage for women who feel sexy during their periods, but whose partner might be squeamish at the sight of blood during intercourse is that there is usually no sign that the woman is menstrsting, so the couple can make love as normal.</p>
						<p>Some women do have difficulty in coming to terms with the idea of putting a rubber dome inside their vaginas. If women suddenly find themselves in a sexual situation and have to insert the diaphragm in a hurry before intercourse, that can interfere with spontaneity. There’s a risk that they may not put it in correctly in a hurry! Some couples have difficulty in remembering to ‘top up’ the spermicide if love-making goes on for several hours, or if they have sex more than once in any one session.</p>
						<p>A few women have an allergy to the material that the diaphragm is made from.&nbsp;<a href="http://www.netdoctor.co.uk/diseases/facts/cystitis.htm" target="_blank" rel="noopener noreferrer">Cystitis</a>&nbsp;(bladder infection) is a problem for a few users – possibly because the front of the diaphragm presses against the urinary passage. Some people are sensitive to spermicides.</p>
						<p><em>Advantages:</em></p>
						<ul>
							<li>You only have to use it when you have sex</li>
							<li>It has no serious health risks</li>
							<li>There’s a choice of different types</li>
							<li>It can be put in at any convenient time before sex</li>
							<li>It may give some protection against&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_cancer.html">cervical cancer</a></li>
						</ul>
						<p><em>Disadvantages</em></p>
						<ul>
							<li>Putting it in at the time of sex can be an interruption</li>
							<li>Some people find the spermicide messy</li>
							<li>Some diaphragm users find they get cystitis (changing to a smaller diaphragm or cap can help)</li>
							<li>Some people are sensitive to the chemicals in latex diaphragms or caps or to the spermicide</li>
							<li>Oil-based lubricants such as body oils or lotions should not be used with latex diaphragms or caps</li>
						</ul>
