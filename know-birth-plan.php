						<p><strong>&nbsp;</strong>It is not compulsory to have a&nbsp;<a href="<?php echo homepage_url() ?>obstetrics_birth.html">birth plan</a>&nbsp;but it may be useful if my patients have particular ideas as to how they would like their deliver to be conducted and especially if they have special requests (eg. no epidurals, no episiotomy if possible, warm tub for pain relief, aromatherapy candles, certain music to be played in your delivery room, husband to cut the cord, intermittent monitoring of the baby, ability to ambulate and not be completely bedbound). It conveys to me what is desired in writing and I will then try to fulfill my patient’s wishes as long as it is not medically dangerous and will not compromise the safety of the delivery. This of course will be discussed point by point before the actual big day so that everyone is clear as to what is expected.</p>
						<ol start="2">
							<li><strong>At what stage should one make a birth plan?</strong></li>
						</ol>
						<p>There is no fixed rule of thumb as to when to draw up a birth plan but I encourage all my patients to discuss their birth plans way before their delivery so that from a very early stage I already have a clear understanding as to how their delivery is to be conducted. More importantly, it gives me plenty of time to make the necessary arrangements. After discussing the birth plan with my patient and her husband, I will send the birth plan to the hospital that she has booked for her delivery so that the hospital and midwives are all aware of my patient’s expectations and anything that the hospital cannot accommodate can be addressed early and alternative arrangements can then be made.</p>
						<ol start="3">
							<li><strong>What are the options, and what is your approach?</strong></li>
						</ol>
						<p>Examples of the options are briefly mention in question 1 already. In most cases, I find that what my patients have requested for in their birth plans are pretty much what I practice as my standard protocol for delivery anyway. It doesn’t really matter how long their birth plan is as long as it is clear and fulfills all their requests. Every pregnancy is a unique experience and so birth plans should be individualized to the mother’s needs and not a one size fit all. Of course the shorter, more succinct and to the point, the better for everyone so as to reduce any confusion.</p>
