						<p><b>Introduction</b></p>
						<p>A well balance healthy diet is always recommended regardless of whether one is pregnant or not. However in pregnancy, a healthy diet becomes even more important than normal as pregnant mothers become the main source of nutrition for their foetus. If women have not adopted this healthy lifestyle then pregnancy would give them a great excuse to start. It just requires a tiny effort on the pregnant woman’s part to be more conscious of what they are eating but the benefits to their baby and themselves is immense.</p>
						<p><b>Should I eat differently now I’m pregnant? (What kinds of food should I&nbsp;</b><b>eat?)</b></p>
						<p>Some women feel that since they are pregnant, they need to eat more in order to “feed” their foetus and sometimes they overdo it by over eating. Being pregnant doesn’t mean that women have to start eating for “two people”. Consuming prenatal vitamins along with a healthy well balanced diet is more than enough. Women should eat properly and not feel that they need to eat constantly in order to provide nutrition for their foetus. Have 3 proper meals and avoid over eating is usually recommended. Increase intake of foods high in fiber and low in fat (orange juice, leafy vegetables) and every diet should include proteins, carbohydrates, vitamins, minerals, and fat.</p>
						<p>Folic acid reduces the risk of neural tube defects. Avoid smoking and alcohol. Regular exercise keeps one healthy and helps to increase bone strength and muscle tone. Exercise during pregnancy helps women to prepare for their labour and childbirth while post natal exercise helps women get back to their pre-pregnancy shape.</p>
						<p><b>Are there any foods I shouldn’t eat during pregnancy?</b></p>
						<p><b></b>Avoid smoking and alcohol as these&nbsp;have a higher risk of miscarriage and stillbirth.&nbsp;Too much alcohol during pregnancy may result in foetal alcohol syndrome, which can cause facial deformities, heart problems, low birth weight and mental retardation. &nbsp;There are some foods that women should try to avoid or reduce consumption if possible during pregnancy. As the studies on the effects of caffeine and miscarriage are conflicting, it is best to limit consumption to 2 cups of coffee per day (not forgetting that caffeine may also interfere with sleep and cause urinary frequency).</p>
						<p>Fish and shellfish are good sources of protein, omega-3 fatty acids, and other nutrients but women should avoid certain types of fish with high mercury content like shark, swordfish and king mackerel. Too much mercury may damage the development of the baby’s nervous system.</p>
						<p>Unpasteurized milk, soft cheese and uncooked meats, poultry and shellfish are associated with listeriosis which leads to miscarriage and stillbirths so it is best to avoid eating these during pregnancy.</p>
						<p><b><br>
					</b><b>Should I take any vitamin supplements?</b></p>
						<p>Pregnant women should take extra iron and folic acid which can be found in prenatal vitamin supplements. They also contain vitamins A, C, and D and minerals, such as zinc and copper.</p>
						<p>In the field of perinatal nutrition, polyunsaturated fatty acids (PUFAs) like omega-3 and omega-6 groups have gained recent attention because of their important functions in foetal and newborn neurodevelopment. Docosahexaenoic acid (DHA) accounts for about 97% of all omega-3 fats in the brain and 93% of all omega-3 fats in the retina. The omega-3 fatty acid eicosapentaenoic acid (EPA) may play an important role in DHA transplacental transport and intracellular absorption. Fish is an excellent source of both DHA and EPA. &nbsp;Because of the concern regarding mercury toxicity with overconsumption of certain fish, pregnant women will need to consume omega-3 fatty acids from 3 sources: vegetable oils (flaxseed, canola, and soybean oil), 2 servings of seafood per week and omega-3 fatty acid supplements containing EPA and DHA.<b><br>
					</b><b><br>
					</b><b>Can I go on a diet?</b></p>
						<p>No, it is not advisable for pregnant women to go on a diet as this will affect foetal growth and well being seeing that the foetus is reliant entirely on the mother for nutrition.</p>
						<p><b>What’s a healthy way to put on weight?</b></p>
						<p>The accepted weight gain for the majority of pregnant women until the time of delivery should range from 11kg to 15kg. 3 to 3.5kg of this will be attributed to the foetus at term. Women should be putting on 900g to 2kg during the first trimester (first 12 weeks) of pregnancy and then approximately 200 to 250g per week for the rest of the pregnancy. On the other hand, being pregnant doesn’t mean that women have to start eating for “two people”. Consuming prenatal vitamins along with a healthy well balanced diet is more than enough. Women should eat properly and not feel that they need to eat constantly in order to provide nutrition for their foetus.</p>
						<p><b>Is it all right to have occasional treats?</b></p>
						<p>Yes of course. One should always try to enjoy one’s pregnancy as much as possible. Everything in moderation and nothing in excess is best after all too much of a good thing then becomes indulgent and extravagant!</p>
						<p><em><b>Ways to stay fit without hurting your baby</b></em></p>
						<p><b>Describe some of the prenatal exercises expecting mums can engage in</b></p>
						<p>Expectant mothers should be encouraged to exercise and most forms of exercise (except contact sports) are generally safe during pregnancy. It is important to remember that if pregnant women were actively exercising before they got pregnant, they have to learn to adjust their regime as their pregnancy advances so as to avoid over stressing themselves. Some of the exercises that pregnant women can do include:</p>
						<ul>
							<li>Walking suits the majority of women as it is gentle on the joints but at the same time involves most muscle groups from the legs and arms to the back. For those women who lead a sedentary lifestyle, this is an excellent way to start exercising.</li>
							<li>Cycling in the gym is also a very popular means of exercise as the risk of falling off your bicycle due to balancing problems as the pregnancy advances is avoided.</li>
							<li>Low impact aerobic exercises are recommended for women who wish to push themselves just that little bit extra.</li>
							<li>Swimming with the help of buoyancy from the water utilises most of the body’s muscles without the impact stresses on the joints. Water also reduces the risk of heat exhaustion as it keeps the body cool. Besides the baby is already “swimming” in the mother’s womb too.</li>
							<li>Running or jogging is fine if women were doing this routinely before getting pregnant. Women should remember that because they are pregnant, they will need to reduce the distance they run or keep the same distance but run at a slower pace so as not to over strain themselves.</li>
							<li>Prenatal yoga is an excellent way to improve one’s posture, muscle tone and strength. It is a good method of relieving backaches and muscle strains during pregnancy. It also helps women to relax and to prepare for childbirth.</li>
						</ul>
						<p>No matter the type exercise, pregnant mothers should ensure that they have ample time to warm up before commencing their exercise and to cool down after their exercise routine. This will reduce the risk of overstraining their muscles, ligaments and joints as well as preventing the body from overheating. If it has been a while since one has exercised then it would be advisable to start off slowly initially.</p>
						<p><b>Why is it good for mums-to-be to keep fit?</b></p>
						<p>Regular exercise keeps one healthy and helps to increase bone strength and muscle tone. Exercise during pregnancy helps women to prepare for their labour and childbirth while post natal exercise helps women get back to their pre-pregnancy shape.</p>
						<p><b>What are some of the health benefits of taking part in prenatal exercises?</b></p>
						<p>Exercise during pregnancy helps to increase muscle tone, strength and stamina. It improves posture and relieves backache, bloating, swelling of the feet and hands in addition to enhancing one’s energy levels. Some women find that they can sleep better after exercise.</p>
						<p><b>Prenatal exercises are appropriate for mums from which stage/week to which stage/week of&nbsp;</b><b>their pregnancy?</b></p>
						<p>Women can exercise throughout their entire pregnancy so long as they do not over exert themselves or over strain their muscle and joints. Pregnant women should listen to their bodies so when one form of exercise doesn’t seem to agree with them, they can change to another. For example I had a patient who ran regularly before her pregnancy. In the first trimester, she continued to run but at a slower pace and at a reduced distance. &nbsp;She then progressed to exercise bicycles and thread mills as her womb increased in size. In the latter part of her second trimester, she found it increasingly more difficult to maintain her balance during these activities so she started swimming. Throughout her pregnancy she was also attending antenatal yoga classes on a regular basis.</p>
						<p><b>What are some of the things mums-to-be should take note of before engaging in these prenatal exercises?</b></p>
						<p>Most women know their physical limits quite well and should never over exert themselves during pregnancy. Before commencing any exercise, women should spend some time warming up with stretching activities. They should wear comfortable clothes and a supportive bra that keeps them cool and won’t impede their movements. It is very important that pregnant mothers hydrate themselves adequately before and after any exercise so as to avoid dehydration or heat stroke. Women should continue to consume their prenatal vitamins and have a well balanced diet to ensure that they have enough nutrition and energy for themselves and their baby.</p>
						<p><b>What should they check with their <a class="h2text" href="<?php echo homepage_url() ?>">pregnancy doctor</a> before commencing their new exercise regime, and when should they stop exercising?</b></p>
						<p>Pregnant mothers should always check with their obstetrician before commencing exercise. This is to exclude any obstetric conditions that would impair them from exercising during pregnancy like preterm labour, preterm rupture of membranes and abnormal vaginal bleeding. A physical examination to exclude any medical conditions like cardiac abnormalities or high blood pressure should be performed before getting to go ahead to exercise.</p>
						<p><b>Has there been an increase in the no. of expecting women enquiring about the various ways that they can keep fit whilst pregnant, compared to several years ago? Why do you think this is so?</b></p>
						<p>I find that more and more women are asking about the safety of exercise during pregnancy in my practice over the recent years. This may be because of the increasing awareness of the benefits of antenatal exercise. Women are also more conscious of the need for healthy lifestyle so the majority of women that enquire about the safety of antenatal exercises are already actively exercising before they conceived and would like to maintain their fitness during pregnancy. The popularity of yoga has also influenced an increase number of women taking up antenatal yoga classes.</p>
