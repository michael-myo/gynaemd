						<p>There is no one method that suits every woman. But the good news is that, unlike in the past, there are many methods to choose from, depending on your lifestyle and personal preferences.</p>
						<p>On one end of the spectrum is sterilisation for men and ligation for women, considered the most effective because they are permanent. Unless you are absolutely sure that you will never want to try for a child again, these are not your options. Many have undergone these procedures only to seek reversal later on because they had a change of heart or circumstances.</p>
						<p>Reversal is not always possible, it is expensive and the tubes can get blocked again over time. Eventually some have had to resort to&nbsp;<a href="http://ivf.gynaemd.com.sg/" target="_blank" rel="noopener noreferrer">IVF</a>(in-vitro fertilisation). At the other end of the spectrum, there are many reversible contraceptive methods and these are either short-term or long-term reversible.</p>
						<p><strong>Long-term reversible options</strong></p>
						<p>Known as “fit and forget” options, the following are considered very effective:</p>
						<p><strong>Implanon NXT</strong>&nbsp;involves implanting a small, plastic rod under the skin of the upper arm via a small incision under local anaesthesia. The rod contains progestine, and the contraceptive effect lasts for three years. It works by preventing ovulation and also thickening of the cervical mucus, which then acts as a barrier to prevent sperm from reaching egg. Implanon NXT is considered one of the most effective birth control methods with a 99% success rate. Once removed, normal menstrual cycles resume rapidly.</p>
						<p><strong>Mirena IUS (intrauterine system)</strong>&nbsp;is an option that offers a longer five-year window of effectiveness. It is a small, flexible plastic T-shaped frame that is inserted into the womb. Everyday, it releases a tiny dose of the hormone levonorgestrel into the uterus, and this prevents ovulation and causes the cervical mucus to thicken and inhibit sperm movement. It also thins the uterine lining, resulting in lighter and shorter periods. Once it is removed, pregnancy becomes possible even within the first month.</p>
						<p><strong>Short-term reversible options</strong></p>
						<p>One common method is the condom. While it is popular, condoms can leak or burst, must be used every time there is intercourse, and can cause allergic reactions. Another option is the pill, with many dosages and types available. In use for more than 50 years, it has evolved vastly to cater to different needs and minimise side effects. Among the best new generation options are Yaz and Yasmin.</p>
						<p>Yaz is taken every day, while Yasmin is taken for 21 days, followed by a 7-day break before resuming again. These hormone-based products are also used to reduce heavy periods, manage menstrual cramps, treat premenstrual syndrome and even acne. These newer generation pills are not associated with weight gain. While the pill protects against certain cancers, such as ovarian and womb cancer, some women have a small increased risk of developing breast cancer while taking it, depending on their own and their family’s health history. This risk will reduce back to their background levels once they cease taking the pill, but the protection against ovarian cancer can last up to 30 years after the patient stops taking the pill.</p>
						<p>Women can also opt for Depo-Provera injections administered every three months. This delays fertility, which means the three-month window must pass before fertility returns.</p>
						<p><strong>Nuvaring</strong>&nbsp;is one of the latest contraceptives. It is a once-monthly vaginally administered contraceptive ring. Once inserted, it releases a low dose of hormones that prevent pregnancy. When used as directed, it is just as effective as the pill, except that you don’t need to remember to take it everyday, and it does not interfere with intercourse. After you insert it, it stays in place for three weeks, and is then removed for one week before a new one is inserted.</p>
						<p>These are some of the many options available. A detailed discussion with your <a href="http://www.gynaemd.com.sg/" title="gynaecologist">gynaecologist</a> will result in the option most suited to your needs.</p>
