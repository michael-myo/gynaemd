					<div class="page-content">

						<p>Most IUDs are now T-shaped. They’re made of plastic and copper. All IUCDs little threads, which hang down a short distance into the vagina. These are useful for checking that the device is still in place and are used when it’s time to remove the IUCD. They work by preventing sperms from getting through the womb and into the tubes, they alter the secretions (mucus) in the cervix, so creating a further barrier for sperms and affect the womb lining – making it less likely to ‘accept’ an egg. The IUCD does not work by ‘causing an abortion’. &nbsp;IUCDs are almost 98 per cent effective.</p>

						<p>IUCDs can make periods heavier, make them longer and may make them more painful. IUCDs are not usually the best&nbsp;<a href="http://www.netdoctor.co.uk/sex_relationships/facts/contraception_which.htm" target="_blank" rel="noopener noreferrer">choice of contraception</a>&nbsp;for women who already have heavy or prolonged periods. They might well do better with another the Pill or the IUS. Most women can use them, but not all. An IUCD is not recommended if:</p>

						<ul>

							<li>There is structural abnormality of the womb or cervix.</li>

							<li>There is a pelvic infection, for instance a&nbsp;<a href="http://www.netdoctor.co.uk/health_advice/facts/venerealdiseases.htm" target="_blank" rel="noopener noreferrer">sexually transmitted disease</a>&nbsp;(STD).</li>

							<li>There is unexplained vaginal bleeding.</li>

							<li>women have heart valve problems</li>

							<li>there is an allergy to copper (which is rare).</li>

							<li>previously had an&nbsp;<a href="http://www.netdoctor.co.uk/diseases/facts/pregnancyectopic.htm" target="_blank" rel="noopener noreferrer">ectopic pregnancy</a>.</li>

							<li>already pregnant.</li>

						</ul>

						<p>IUCDs are really best for women who have already had a pregnancy – partly because the cervix is wider open, making the insertion of the device easier and less painful. IUCDs last from 3 to 5 years before they need to be changed.</p>

						<p>Advantages:</p>

						<ul>

							<li>It works as soon as it is put in</li>

							<li>It works for 3 to 5 years depending on type</li>

							<li>It doesn’t interrupt sex</li>

							<li>It can be used if breastfeeding</li>

							<li><a href="https://www.gynaemd.com.sg/gynaecology_infertility.html" title="normal female fertility">normal female fertility</a> returns as soon as the IUCD is removed</li>

							<li>It’s not affected by other medicines</li>

						</ul>

						<p>Disadvantages:</p>

						<ul>

							<li>periods may be longer, heavier or more painful – this may improve after a few months</li>

							<li>need an internal examination to check if it’s suitable, and when it is fitted</li>

							<li>There is a very small chance of infection in the first 20 days after the IUD is put in</li>

							<li>The IUCD may come out (expulsion) or it may move (displacement) – this is more likely to happen shortly after it has been put in</li>

							<li>The IUD may go through (perforate) the cervix or womb when it’s put in, but the risk is low when it’s put in by an experienced doctor</li>

							<li>If women become pregnant, there is a small increased risk of having an ectopic pregnancy (a pregnancy occurring outside the womb, normally in the fallopian tube). This risk is less than in women using no&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_contraception.html">contraception</a></li>

						</ul>

