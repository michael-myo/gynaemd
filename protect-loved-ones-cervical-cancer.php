						<p align="justify">A woman dies of cervical cancer every two minutes worldwide. Learn how you can protect yourself and your loved ones.</p>
						<p align="justify">Cervical Cancer is the sixth most common cancer affecting women in Singapore. It occurs when the cells of the cervix, which is the neck of the womb, change in a way that leads to abnormal growth and invasion into other tissues or organs in the body.</p>
						<p align="justify"><strong>CAUSES</strong></p>
						<p align="justify">Cervical cancer is not a hereditary condition. The majority of cases are caused by a human papilloma virus (HPV), which is a very common virus. In the USA, up to 50% of sexually active may be infected with an HPV at some time in their lives. HPV infection does not lead to long-term natural immunity, so every woman is at risk</p>
						<p align="justify">Ninety percent of women are able to clear HPV infection within two years. In 10% of women with persistent or recurrent HPV infections, it will progress to pre-cancer and finally cervical cancer if nothing is done to detect this change and to treat it.</p>
						<p align="justify">The virus triggers alterations in the cells of the cervix, leading to pre-cancer changes called CIN I, II, and III. These changes occur mainly in women between the ages of 20 to 40. If this is not detected or treated, it will progress to cervical cancer – occurring mainly between the ages of 30 and 50. It takes months to develop CIN but about 10 years to progress to cancer.</p>
						<p align="justify"><strong>PREVENTION</strong></p>
						<p class="h2text"><a href="<?php echo homepage_url() ?>abt_us_doctor_writeup.html">Dr. Christopher Ng, Obstetrician and Gynaecologist at GynaeMD Women’s &amp; Rejuvenation Clinic,</a></p>
						<p align="justify">recommends that women should get vaccinated to boost immunity. “Primary prevention is always best. Being immune to HPV infections would reduce the risk of cervical pre-cancer (CIN) and cancer, he adds. Gradualism also prevents genital warts in addition to cancer protection.</p>
						<p align="justify"><strong>SYMPTOMS</strong></p>
						<p align="justify">Early stages of cervical cancer often show no symptoms. As it slowly progresses, symptoms may include:</p>
						<ul>
							<li>Abnormal bleeding from the vagina, or change in menstrual cycle. (Often mistaken as a result of hormonal imbalance.)</li>
							<li>Bleeding after sex.</li>
							<li>In post-menopausal women there can be post-menopausal bleeding.</li>
							<li>Pain during sex. (Often mistaken as a result of dryness or infection.)</li>
							<li>Unpleasant-smelling vaginal discharge. (Often mistaken as a result of vaginal infections)</li>
						</ul>
						<p>I here may also be non-specific symptoms like loss of appetite, weight loss, heavy menses, pelvic pain, back pain, leg pain and fatigue.</p>
						<p><strong>DIAGNOSIS</strong></p>
						<p>Routine pap smears are still the most common method for detecting&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_cancer.html">cervical cancer</a>. It accurately detects over 90% of cervical cancers, even before symptoms develop. A colposcopy and biopsy is another method and is usually performed following a pap smear that shows abnormal results. This provides a magnified view of the abnormal cervix. In this minor procedure, acetic acid and iodine is applied to the cervix to highlight the abnormal areas and biopsies of the abnormal areas are then taken for further evaluation.</p>
						<p><strong>RECOMMENDATION</strong></p>
						<p>Says Dr Ng: “The minimum recommendation is to undergo one pap smear every three years until age 65 provided there are no abnormalities. Regular pap smears are universally accepted as the gold standard for early detection, diagnosis and treatment. The purpose of a pap smear is to collect cells from the cervix. The relatively painless procedure is performed using either a wooden spatula or plastic brush. This can detect pre-cancerous changes which can be removed before they become cancerous.</p>
