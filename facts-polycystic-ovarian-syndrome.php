						<p>Polycystic Ovarian Syndrome (PCOS) is one of the most common female endocrine disorders. It is a condition that affects women of reproductive age. The affected women commonly have enlarged ovaries with multiple small cysts along the periphery (although this is not exclusive to every woman). This gives the ovaries a polycystic appearance.</p>
						<p>While the exact cause of PCOS is unknown, there may be a hereditary element involved and women with PCOS may have an association with low grade inflammation and insulin resistance (a result of an inability to use insulin effectively).</p>
						<p><strong>Women with PCOS usually have at least two of the following symptoms.</strong></p>
						<ul>
							<li>There may be ultrasound scan appearance of multiple cysts around the periphery of the ovary. It is important to remember that some women do have other symptoms of PCOS without the ultrasound appearance.</li>
							<li>Menstrual irregularities – the most common symptom. This is defined as experiencing less than eight periods a year, having menstrual cycles that are more than 35 days, having prolonged periods that may be heavy or scant, or going four or more months without periods. This may be associated with anovulation (not ovulating).</li>
							<li>Signs of excessive androgen (male hormones) with the accompanying male pattern hair baldness, excessive facial and or body hair (hirsutism), adolescent acne or acne in adulthood. This may vary from race to race though.</li>
						</ul>
						<p>These symptoms can occur during menarche (when adolescent girls first start having their periods) or later in the reproductive life. The signs and symptoms vary from person to person as does the severity. The diagnosis of the condition is made after taking a detailed history and performing an examination, hormonal blood tests and a pelvic ultrasound scan.</p>
						<p>The treatments available to treat PCOS are targeted at the various symptoms and conditions that affect each woman. For instance, hirsutism and acne can be treated with oral contraceptive pills (Yaz, Yasmin, and Diane35) which have anti-testosterone effects.</p>
						<p>For women who do not ovulate and are experiencing&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_infertility.html">infertility</a>&nbsp;as a result, they can be stimulated to ovulate with clomiphene citrate taken in the early part of the menstrual cycle. If this fails to stimulate ovulation, gonadotrophin injections (Puregon or Gonal F) may then be used if the woman chooses to proceed with superovulation intrauterine insemination (SOIUI) or&nbsp;<a href="https://www.gynaemd.com.sg/gynaecology_infertility.html" target="_blank" rel="noopener noreferrer">in-vitro fertilization (IVF)</a>. If gonadotrophin injections still do not induce ovulation, then laparoscopic ovarian drilling (key-hole surgery) performed as a day surgical procedure may be used in an attempt to induce ovulation and to increase the response to gonadotrophin stimulation.</p>
						<p>For those suffering from irregular menses, they can be prescribed a course of progesterone hormones to regulate their period. If they do not want to get pregnant at the same time then low dose oral contraceptive pills would be more appropriate (Yaz, Yasmin, Mercilon, Marvelon). These hormonal medications will not only help to regulate the period but also reduce the risk of endometrial cancer as well. Sometimes metformin (an oral medication to treat type 2 diabetes) may be added to improve ovulation and to regulate menstrual cycles.</p>
						<p>There are, however, possible complications that may occur to women with PCOS. Women with PCOS are predisposed to several conditions, especially if they are obese. These include elevated triglycerides and low HDL cholesterol (good cholesterol) along with a tendency to develop high blood pressure and type 2 diabetes mellitus later in life. They have an increased risk of cardiovascular disease as a result. They are also at a higher risk of gestational diabetes (diabetes in pregnancy) and pregnancy induced high blood pressure if they are pregnant. These women tend to experience abnormal menstrual bleeding and this may be associated with anovulation (not ovulating) and therefore infertility. Continuous exposure to high oestrogen levels may increase the risk of developing uterine (womb) cancer in the future.</p>
						<p>As some of the symptoms of PCOS are thought to be related to insulin resistance, having a healthy diet consisting of high fibre and complex carbohydrates may help as these foods are slowly digested by the body resulting in a slower rise in blood glucose levels and therefore insulin. Reducing obesity and keeping one’s weight within the recommended range helps to reduce insulin and androgen levels and so hopefully restore ovulation in the process. Aside from diet, regular exercise will not only help to keep one’s weight in check but also reduce insulin resistance.</p>
