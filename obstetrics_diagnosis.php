					<div class="green-border-box">
						<p></p>
						<ul>
							<li>Chorionic villus sampling (CVS) is a prenatal test that detects chromosomal abnormalities (eg. Down syndrome) and a host of other genetic disorders (eg. cystic fibrosis, sickle cell disease and thalassemia). Cells are taken from tiny finger-like projections on the placenta called chorionic villi and are sent to a lab for genetic analysis.</li>
							<li>The main advantage of CVS over&nbsp;<a title="" href="http://www.babycenter.com/0_amniocentesis_327.bc">amniocentesis</a>&nbsp;is that it can be performed earlier — generally between 11 and 12 weeks of pregnancy so you can find out about your baby’s condition sooner.</li>
							<li>The miscarriage rate associated with CVS is between 0.5 to 1% and the risk of fetal limb abnormalities is minimised when performed after 11 weeks.</li>
							<li>Unlike amniocentesis, CVS cannot test for neural tube defects as no amniotic fluid is removed.</li>
							<li>Results are usually available in a few days to about 7 to 10 days.<br>
