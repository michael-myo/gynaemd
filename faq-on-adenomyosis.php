						<p>Though Adenomyosis is considered a benign (not life-threatening) condition, the frequent pain and heavy bleeding associated with it can have a negative impact on a woman’s quality of life.</p>
						<p><strong>Q: What is Adenomyosis?</strong></p>
						<p>Adenomyosis is a common medical condition characterized by growth of the endometrium (the tissue that normally lines the womb) into the muscle layers of the womb.</p>
						<p><strong>Q: What are the causes, symptoms and treatment options?</strong></p>
						<p>The cause is thought to be hormonally related. The symptoms of endometriosis often worsen over time. Women may complain of heavy and sometimes irregular menses, painful periods, pain during sex (this is a deep pain during penetration) and pain in the pelvis or lower abdomen in between periods. It may be associated with infertility (about 30 to 40 per cent of women with adenomyosis and endometriosis are infertile). Pain as a result of adenomyosis can mimic the pain caused by appendicitis for example, especially if it is on the right side. The treatment options are divided into either medical or surgical remedies.</p>
						<p><strong>Q: Are there any medications that I can take to improve the symptoms? What side effects should I expect from using medication?</strong></p>
						<p>A variety of treatments can help control the symptoms of adenomyosis but nothing can completely prevent or cure it. Treatment is directed at either relief of pain or&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_infertility.html">infertility</a>. The treatment for infertility would be SOIUI (superovulation intrauterine insemination) or IVF (in vitro fertilisation). Some treatment options for pain are: Analgesics (NSAIDs), combined oral contraceptive pill (Yaz, Yasmin), Intrauterine system IUS (Mirena), Danazol, oral progesterone (Visanne), Depot progesterone injections (Depoprovera), gonadotropin-releasing hormone (GnRH) agonists (Lucrin, Zoladex).</p>
						<p>They are equally effective but their side-effects and cost profiles differ. NSAIDs can lead to gastric ulcers so they should be consumed with antacids. Side effects of hormonal medications like Yaz, Yasmin, Mirena, Depoprovera and Visanne may include nausea, headache, breast discomfort and deep vein thrombosis. Long term use of Danazol is not advisable as it may lead to permanent voice changes for women. Suppression of ovarian function with GnRH agonists for several months reduces adenomyosis-associated pain but can also lead to menopausal side effects (hot flushes, night sweats, moodiness, insomnia, irritability, osteoporosis) and so cannot be used long term.</p>
						<p><strong>Q: Under what circumstances do you recommend surgery?</strong></p>
						<p>Surgery is advisable if the adenomyotic symptoms like pain is severe or if infertility is thought to be a result of pelvic adhesion and blocked tubes. The goal of surgery is to remove any concurrent endometriotic cyst and adenomyotic lesions as well as any associated adhesions in order to restore normal anatomy. Removal of adenomyosis and adhesions to improve fertility is effective. Pregnancy rates are highest within a year of surgery, since adenomyosis commonly recurs to various extents in spite of the operation.</p>
						<p>For some women who have completed their family and have severe pain that is not relieved with medical treatment, surgery can be performed to remove the womb (hysterectomy).</p>
						<p><strong>Q: Could the condition affect my ability to become pregnant? Are there any alternative treatments for me to try out?</strong></p>
						<p>If the tubes are damaged then fertility will be affected. In this case, SOIUI or IVF may be required in women who fail to conceive after some time. Treatment with SOIUI improves fertility in minimal to mild adenomyosis but tubal patency is a prerequisite. IVF is an appropriate treatment, especially if the tubes are blocked, if there is also male factor infertility, and/or other treatments have failed. It is advisable for women who have adenomyosis not to delay having children because adenomyosis tends to worsen with time. The longer you have it, the greater your chance of becoming infertile.</p>
						<p>For some women, pregnancy can lessen the symptoms and effects of adenomyosis. The reality is that pregnancy, like hormonal drug treatments, usually suppresses the symptoms of adenomyosis but does not eradicate the disease itself. Symptoms may or may not recur after the birth of the child. Most women can delay the return of symptoms by breastfeeding, but only while the breastfeeding is frequent enough and intense enough to suppress the menstrual cycle.</p>
