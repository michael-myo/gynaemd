						<h2>Know the possible signs of infertility and seek help early</h2>
						<p><img class="alignnone size-full wp-image-657" src="<?php echo homepage_url() ?>dist/images/c2143j1-520x245.jpg" alt="" width="520" height="245" srcset="<?php echo homepage_url() ?>dist/images/c2143j1-520x245.jpg 520w, <?php echo homepage_url() ?>dist/images/c2143j1-520x245-300x141.jpg 300w" sizes="(max-width: 520px) 100vw, 520px"></p>
						<p><a href="<?php echo homepage_url() ?>gynaecology_infertility.html">INFERTILITY</a>&nbsp;is defined as the inability to conceive after one year of having regular intercourse without using contraception. If a couple has not been successful after a year, they should see a&nbsp;<a href="<?php echo homepage_url() ?>">gynaecologist</a>&nbsp;to get investigated for possible causes for their infertility.</p>
						<p>However, as the risk of genetic disorders, miscarriage and infertility increase with increasing age, women who are over 35 years old and have been unsuccessful may wish to seek medical help after six months of contraceptive-free intercourse for these very reasons. Infertility may be cause by more than one factor. Some are easy to find and treat, while others are not. The causes may relate to the woman or the man. In some cases, no cause can be found in either partner.</p>
						<p>Eating healthy food and staying well nourished increases your odds of conceiving. Make sure to include enough protein, iron, zinc, folic acid and vitamin C, because deficiencies in these nutrients have been linked to decrease fertility and a higher risk of early miscarriage. Avoiding alcohol and reducing caffeine intake certainly helps as alcohol consumption decreases the ability to get pregnant (not to mention the harm it can cause to a developing foetus). High caffeine consumption can increase the risk of miscarriage.</p>
						<p>Quit smoking because aside from health concerns, cigarette toxins can damage a woman’s eggs, interfering with the fertilisation and implantation. It also affects sperm production and quality.</p>
						<p>The decision to begin testing depends on a number of factors. They include woman’s age and her partner’s age, and how long they have been trying to get pregnant. Testing involves an evaluation including physical examination, medical history, semen analysis, ovulation check, tests to check for a normal uterus (ultrasound scan) and patent fallopian tubes (non-surgical hysterosalpingography HSG or by surgical laparoscopy and hydrotubation), followed by discussion about how often and when you have sex.</p>
						<p>A semen analysis is a key part of the basic checkup for a man. The semen sample is obtained by masturbation and is analysed for sperm number, shape, movement, and signs of infection.</p>
						<p>The investigation for a woman begins with a thorough physical examination and detailed health history. The health history will focus on these key points: menstrual function, such as irregular bleeding and pain, pregnancy history, STD (sexually transmitted disease) history, and past history of contraceptive.</p>
						<p><b>THERE ARE MANY TESTS TO SEE IF OVULATION OCCURS. THESE INCLUDE:</b>
							<br>
							<b>Urine Test.</b>&nbsp;This test can be done by the woman at home with a kit. It is a way to predict ovulation. This test measures a rise in luteinising hormone (LH) indicating that ovulation has occurred.</p>
						<p><b>Basal Body Temperature.</b>&nbsp;Women take their temperature by mouth every morning before getting out of bed (basal temperature). After a woman ovulates, her body temperature increases.</p>
						<p><b>Serum Progesterone.</b>&nbsp;This is a timed blood test done at specific times during a woman’s menstrual cycle to determine if she has ovulated.</p>
						<p><b>TREATMENTS FOR INFERTILITY INCLUDE:</b>
							<br>
							<b>Ovulation Induction.</b>&nbsp;If the woman does not ovulate, oral or injectable medications can be used to stimulate ovulation. If there are no other problems, more than half of such women get pregnant within six treatment cycles.</p>
						<p><b>Surgery.</b>&nbsp;If the fallopian tubes are not patent, surgery may be performed to unblock them, Surgery also may be done to remove growths such as polyps or fibroids; remove scarring and adhesions from a previous surgery, infection, or endometriosis. If the problem is with the man’s blocked sperm ducts, surgery can sometimes fix it.</p>
						<p><b>Assisted Reproductive Technologies.</b>&nbsp;The first is Superovulation Intrauterine Insemination (SOIUI), which is offered for mild male factor infertility, mild endometriosis, cervical factor infertility, or unexplained infertility, and after several failed clomid cycles. The other is&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_infertility.html">In vitro fertilisation</a>&nbsp;(IVF), where sperm is used to fertilise eggs outside the woman’s body. These eggs are obtained from the woman via a minor surgical procedure performed in a fertility lab. This is offered to women with irreparably damaged fallopian tubes, blocked fallopian tubes, severe endometriosis, ovulatory dysfunction, unexplained infertility and severe sperm disorders.</p>
