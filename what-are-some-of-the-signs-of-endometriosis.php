						<p><strong>What are some of the signs of endometriosis? Regina/Pauline mentioned in her testimonial that she had persistent coughing and a sharp pain in her abdomen. How would females be able to tell endometriosis apart from other medical conditions? What are the symptoms most similar to?</strong></p>
						<p><a href="<?php echo homepage_url() ?>gynaecology_general.html">Endometriosis</a>&nbsp;is a common medical condition characterized by growth of&nbsp;<a href="http://en.wikipedia.org/wiki/Endometrium" target="_blank" rel="noopener noreferrer">endometrium</a>&nbsp;(the tissue that normally lines the&nbsp;<a href="http://en.wikipedia.org/wiki/Uterus" target="_blank" rel="noopener noreferrer">uterus</a>) beyond or outside the uterus. It looks and acts like tissue in the uterus. It most often appears in places within the pelvis:</p>
						<ul>
							<li>Ovaries</li>
							<li>Fallopian tubes</li>
							<li>Surface of the uterus</li>
							<li>Cul-de-sac (the space behind the uterus)</li>
							<li>Bowel</li>
							<li>Bladder and ureters</li>
							<li>Rectum</li>
						</ul>
						<p>Endometrial tissue may attach to organs in the pelvis or to the&nbsp;<em>peritoneum</em>, the tissue that lines the inside of pelvis and abdomen. In rare cases, it also may be found in other parts of the body. Endometrial tissue that grows in the ovaries may cause a cyst (endometriotic cyst) to form. Endometrial tissue outside the uterus responds to changes in hormones. It breaks down and bleeds like the lining of the uterus during the menstrual cycle. The breakdown and bleeding of this tissue each month can cause scar tissue, called adhesions. Adhesions can cause pain. Sometimes, adhesions bind organs together. The symptoms of endometriosis often worsen over time</p>
						<p>Women may complain of the following:</p>
						<ol>
							<li>a) Heavy and sometimes irregular menses</li>
							<li>b) Painful periods</li>
							<li>c) Pain during sex (this is a deep pain during penetration)</li>
							<li>d) Pain in the pelvis or lower abdomen in between periods</li>
							<li>e) It may be associated with infertility.</li>
						</ol>
						<p>It is estimated that 30-40% of women with endometriosis may have difficulties in becoming pregnant i.e. unable to conceive after 1 year of regular intercourse. The age-dependent cycle fecundity (monthly) rates in healthy fertile women range between 15 and 25%. That chance is less than 1% for women with severe endometriotic disease.</p>
						<p>Endometriosis pain can mimic the pain caused by appendicitis for example especially if it is on the right side (I had a patient that presented this way and also ended up needing an emergency operation and hormonal injections post op.) This can be distinguished by performing ultrasound scans and CT scans which will be able to see the enlarged endometriotic cyst or an inflamed appendix. &nbsp;The cyclical nature of the pain points towards endometriosis rather than other pathological causes of pelvic pain (ureteric colic, bladder stones, bowel disorders).</p>
						<p><strong>Under what conditions would tumours in the endometrium get cancerous? When would it be considered too dangerous to operate on it?</strong></p>
						<p>The causes of endometriosis are still unknown and its spread is related to female hormones so much so that endometriosis can affect any menstruating woman, from the time of her first period to menopause, regardless of her race or ethnicity, or her socio-economic status. It does not cause cancer. Surgery is advisable if the endometriotic symptoms like pain is severe, the endometriotic cyst is enlarged or for fertility investigation and treatment. The only instance in which surgery cannot be performed is if the patient is deemed to be unfit for surgery which is very rare.</p>
						<p><strong>Based on past studies, are there any trends to show that Asian women are more prone to getting endometriosis as opposed to women in other countries? Globally, do you know how many women this condition affects?</strong></p>
						<p>Although the disease affects women of all races, there is some evidence to suggest that Asian and Caucasian women are at greater risk of developing endometriosis than other races. Because endometriosis is such a variable disease and because it is often misdiagnosed and under diagnosed, the true incidence is not known but it is estimated to occur in about 1 in 10 women of child-bearing age.</p>
						<p><strong>What kinds of test are run on patients, and what are their chances of success?</strong></p>
						<p>Besides a thorough history to determine if women have any characteristic symptoms to suggest the presence of endometriosis, the gynaecologist can perform a pelvic exam. This is to try to localize the area of pelvic pain. Other causes of pelvic pain would have to be ruled out. A pelvic ultrasound scan can be used to detect endometriotic cysts and this is usually combined with blood Ca125 investigation (which can be raised in endometriosis).</p>
						<p>Endometriosis can be mild, moderate, or severe. The extent of the disease can be confirmed by looking directly inside the pelvis using a laparoscope (key hole surgery) under general anesthesia. The endometriotic lesions as well as endometriotic cysts and adhesions can also be removed during a laparoscopy.</p>
						<p><strong>How does Visanne come into play?</strong></p>
						<p>Visanne contains 2mg of dienogest (a progestagen) and is taken once a day. Studies have shown that Visanne within 4 weeks of starting provides long lasting pain relief associated with endometriosis and reduces endometriotic lesions.</p>
						<p><strong>What is the recovery period for patients who have successfully been operated upon?</strong></p>
						<p>If the surgery is performed laparoscopically, most patients are discharged after 1-2 days. For open surgery, the hospital stay is typically 2-3 days or more. Most women recover within 2 weeks for laparoscopy and up to 1 month for open surgery.</p>
						<p><strong>I was hoping that you’d be able to highlight four other diseases or conditions that women shouldn’t ignore. We would like this to serve as a warning to our young readers. What are the symptoms of each and what can be done to cure the condition? If left untouched, what complications could arise?</strong></p>
						<p>1) All sexually active women should have pap smears performed routinely every 1-3 years depending on their results as this is the recommended method for cervical cancer screening. It accurately detects 90% of cervical cancers, even before symptoms develop. The early stages of&nbsp;<a href="http://www.preventcervicalcancer.ie/glossary.asp?#Cervical_cancer" target="_blank" rel="noopener noreferrer">cervical cancer</a>&nbsp;often cause no symptoms. This is why it is so important to have regular&nbsp;<a href="http://www.preventcervicalcancer.ie/glossary.asp?#Cervical" target="_blank" rel="noopener noreferrer">cervical</a>pap&nbsp;<a href="http://www.preventcervicalcancer.ie/glossary.asp?#Smear" target="_blank" rel="noopener noreferrer">smear tests</a>. If cervical cancer does cause any symptoms, the most common one is abnormal bleeding from the vagina, either between periods (mistaken as a result of hormonal imbalance) or after sex (mistaken as a result of dryness or infection). Other symptoms include unpleasant-smelling vaginal discharge (mistaken as a result of vaginal infections) and pain during sex (mistaken as a result of dryness).</p>
						<p>2) Getting vaccinated with Gardasil or Cervarix vaccines can reduce the risk of developing cervical cancer so women who are not pregnant or have not developed cervical cancer should get themselves vaccinated with either Gardasil or Cervarix vaccines. Cervical cancer is not a hereditary condition. In fact, the majority of cases are caused by a virus called Human Papillomavirus or HPV. HPV is a very common virus and it was found that in USA, up to 50% of couples who have ever been sexually active are likely to be infected with an HPV virus at some time in their lives. As these vaccines prevent the initial HPV infection at the point of exposure, in some countries girls for the age of 12 are routine vaccinated at school.</p>
						<p>3) Vaginal infections or&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=5955" target="_blank" rel="noopener noreferrer">vaginitis</a>&nbsp;is an inflammation of the&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=5951" target="_blank" rel="noopener noreferrer">vagina</a>&nbsp;that creates&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=3010" target="_blank" rel="noopener noreferrer">discharge</a>, odour, irritation or&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=4060" target="_blank" rel="noopener noreferrer">itching</a>. The 3 commonest vaginal infections are bacterial vaginosis,&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=24474" target="_blank" rel="noopener noreferrer">yeast infection</a>&nbsp;and <a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=58880" target="_blank" rel="noopener noreferrer">trichomoniasis</a>&nbsp;&nbsp;vaginalis.</p>
						<ol>
							<li>i) Bacterial vaginosis causes an abnormal vaginal discharge with an unpleasant odour. Some women report a strong fishlike smell, especially after intercourse. The discharge is usually white or gray, it can be thin. They may also have burning during urination or itching around the outside of the vagina, or both. Some women with bacterial vaginosis have no symptoms at all.</li>
							<li>ii) Yeast infections or candidiasis cause a thick, whitish-gray “cottage cheese” type of vaginal discharge and may be itchy. You may have intense itching in your genitals.&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=24969" target="_blank" rel="noopener noreferrer">Painful urination</a>&nbsp;and intercourse are common. You may not always have a vaginal discharge. Men with genital candidiasis may have an itchy rash on the&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=4827" target="_blank" rel="noopener noreferrer">penis</a>. Most male partners of women with yeast infection do not experience any symptoms of the infection.</li>
						</ol>
						<p>iii) Trichomoniasis causes a frothy vaginal discharge that may be yellow-green or gray, itching and irritation of the genitals, burning with urination (sometimes confused with a&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=5914" target="_blank" rel="noopener noreferrer">urinary tract infection</a>), discomfort during intercourse, and a foul smell. Because trichomoniasis is a sexually transmitted disease, symptoms may appear within 4-20 days after exposure. Men rarely have symptoms, but if they do, they may have a thin, whitish discharge from the penis and painful or&nbsp;<a href="http://www.emedicinehealth.com/script/main/art.asp?articlekey=24970" target="_blank" rel="noopener noreferrer">difficult urination</a>.</p>
						<p>There are many other types of vaginal infections besides these 3 but regardless of the cause, women should seek treatment (antibiotics, antifungals or a combination of both) from their gynaecologist sooner rather than later as some infections may damage fallopian tubes and therefore cause infertility or ectopic pregnancies in the future.</p>
						<p>4) PMS (premenstrual syndrome) is a disorder characterized by a set of hormonal changes that trigger disruptive symptoms in a significant number of women for 7 to 10 days before menstruation and disappear a few days after menses. PMS are a natural part of the&nbsp;<a href="http://www.webmd.com/hw-popup/menstrual-cycle" target="_blank" rel="noopener noreferrer">menstrual cycle</a>, affecting 1 in every 5 women in asia pacific at some time during their lives.</p>
						<p>PMS symptoms vary greatly from woman to woman and cycle to cycle, and can range from mild to severe. Some women note that their symptoms are worse during times of increased emotional or physical stress. Of the more than 150 symptoms that have been linked to PMS, the most common are listed below.</p>
						<p>Physical symptoms include:</p>
						<ul>
							<li>Breast swelling and tenderness.</li>
							<li>Bloating, water retention, weight gain.</li>
							<li>Changes in bowel habits.</li>
							<li>Acne.</li>
							<li>Food cravings, especially for sweet or salty foods.</li>
							<li>Sleep pattern changes.</li>
							<li>Fatigue, lack of energy.</li>
							<li>Decreased sexual desire.</li>
							<li>Pain. Common complaints include headaches or migraines, breast tenderness, aching muscles and joints, or cramps and low back pain prior to menstrual bleeding.</li>
						</ul>
						<p>Behavioral symptoms include:</p>
						<ul>
							<li>Aggression.</li>
							<li>Withdrawal from family and friends.</li>
						</ul>
						<p>Emotional and cognitive symptoms include:</p>
						<ul>
							<li>Depression, sadness, hopelessness.</li>
							<li>Anger, irritability.</li>
							<li>Anxiety.</li>
							<li>Mood swings.</li>
							<li>Decreased alertness, inability to concentrate.</li>
						</ul>
						<p>Premenstrual dysphoric disorder, or PMDD, is a severe form of premenstrual syndrome (PMS). The symptoms of PMDD are similar to those of PMS, but are severe enough to interfere with work, social activities, and&nbsp;<a href="http://www.webmd.com/sex-relationships/guide/default.htm" target="_blank" rel="noopener noreferrer">relationships</a>. PMDD occurs in 2-10% of menstruating women. Women with a personal or family history of&nbsp;<a href="http://www.webmd.com/depression/default.htm" target="_blank" rel="noopener noreferrer">depression</a>&nbsp;or&nbsp;<a href="http://www.webmd.com/depression/postpartum-depression/default.htm" target="_blank" rel="noopener noreferrer">postpartum depression</a>&nbsp;are at greater risk for developing PMDD. Most researchers believe that PMDD is brought about by the hormonal changes related to the&nbsp;<a href="http://women.webmd.com/tc/normal-menstrual-cycle-topic-overview" target="_blank" rel="noopener noreferrer">menstrual cycle</a>. Some modern oral contraceptive pills are actually used to treat PMS and PMDD (eg. Yaz which is currently the only and the world’s first OCP that is approved for this very purpose).</p>
