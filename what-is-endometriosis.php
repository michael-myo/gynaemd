						<p><a href="https://www.gynaemd.com.sg/what-is-endometriosis.html" title="Endometriosis" >Endometriosis</a> is a common medical condition characterized by the growth of endometrium (the tissue that normally lines the womb) beyond or outside the uterus. It most often appears in places within the pelvis, such as ovaries, fallopian tubes, surface of the uterus, pouch of Douglas (the space behind the uterus), bowel, bladder and ureters or rectum.</p>

						<p>Endometrial tissue may attach to organs in the pelvis or to the peritoneum (the tissue that lines the inside of pelvis and abdomen). In rare cases, it also may be found in other parts of the body.</p>

						<p>Endometrial tissue outside the uterus responds to changes in hormones. It breaks down and bleeds like the lining of the uterus during the menstrual cycle. The breakdown and bleeding of this tissue each month can cause scar tissue, called adhesions which sometimes bind organs together.</p>

						<p>Some of the symptoms of endometriosis include:</p>

						<ul>

							<li>Heavy and sometimes irregular menses</li>

							<li>Painful periods</li>

							<li>Pain during sex (this is a deep pain during penetration)</li>

							<li>Pain in the pelvis or lower abdomen in between periods</li>

							<li><a href="<?php echo homepage_url() ?>gynaecology_infertility.html">Infertility</a>&nbsp;(about 30% to 40% of women with endometriosis are infertile)</li>

						</ul>

						<p>This tends to affect women of reproductive age and so is more common in women between 20s to 40s. Although the disease affects women of all races, there is some evidence to suggest that Asian and Caucasian women are at greater risk of developing endometriosis than other races. Because endometriosis is such a variable disease and because it is often misdiagnosed and under diagnosed, the true incidence is not known but it is estimated to occur in about one in 10 women of child-bearing age.</p>

						<p>There are treatments that can help control the symptoms of endometriosis but nothing can completely prevent or cure it. There is evidence that taking oral contraceptives (birth control pills) may help keep endometriosis from developing by suppressing endogenous hormonal production. As endometriosis is hormonally related, there is no permanent cure for it. But there are rare cases of endometriosis found in menopausal women.</p>

						<p>For some women, pregnancy can lessen the symptoms and effects of endometriosis. Pregnancy usually suppresses the symptoms of endometriosis but does not eradicate the disease itself. Symptoms may or may not recur after the birth of the child. Most women can delay the return of symptoms by breastfeeding, but only while the breastfeeding is frequent enough and intense enough to suppress the menstrual cycle. Specialists advise women with endometriosis not to delay having children because this disease tends to worsen with time. The longer you have endometriosis, the greater your chance of becoming infertile.</p>

						<p>Treatment is directed at either relief of pain or infertility. The treatment options for pain range from:</p>

						<ul>

							<li>Analgesics (NSAIDs)</li>

							<li>Combined oral contraceptive pill (Yaz, Yasmin)</li>

							<li>Intrauterine system IUS (Mirena)</li>

							<li>Danazol</li>

							<li>Oral progesterone (Visanne)</li>

							<li>Depot progesterone injections (Depoprovera)</li>

							<li>Gonadotropin-releasing hormone (GnRH) agonists (Lucrin, Zoladex)</li>

						</ul>

						<p>They are equally effective but their side-effect and cost profiles differ. Suppression of ovarian function with any of these medications for several months reduces endometriosis-associated pain.</p>

						<p>Endometriosis can cause the fallopian tubes to become blocked and can damage the ovaries. It is estimated that 30-40% of women with endometriosis may have difficulties in becoming pregnant i.e. unable to conceive after 1 year of regular intercourse. The age-dependent cycle fecundity (monthly) rates in healthy fertile women range between 15 and 25%. That chance is less than 1% for women with severe endometriotic disease.</p>

						<p>Surgery is advisable if the pain is severe, endometriotic cyst is enlarged or for fertility investigation and treatment. The goal of surgery is to remove or coagulate all visible endometriotic peritoneal lesions, endometriotic ovarian cysts, deep rectovaginal endometriosis and associated adhesions, and to restore normal anatomy.</p>

						<p>Following successful surgery, most women should report a significant improvement in their pelvic pain, menstrual abnormalities and hopefully an improvement in fertility rates. For some women with severe endometriosis who are not seeking to get pregnant after surgery, they can be treated with oral progesterone (Visanne), depot progesterone injections (Depoprovera) or gonadotropin-releasing hormone (GnRH) agonists (Lucrin, Zoladex) to reduce the risk of endometriosis recurrence.</p>

