						<p><a href="<?php echo homepage_url() ?>gynaecology_menopause.html">Menopause</a>&nbsp;is a natural biological process that affect women of a certain age. It is defined as occurring 12 months after the last menstrual period and marks the end of the menstrual cycles. Natural fertility ends in that the ovaries have no more eggs.</p>
						<p>This means that menopause is for the most part considered complete when a lady has not had a period for one year. It normally happens between the ages of 45 – 55 years with the normal age being 52 years.</p>
						<p><strong>What is menstruation and menstrual cycle?</strong></p>
						<p>Puberty occurs when a girl becomes sexually mature. It is a process that usually affects girls between the ages of 10 to 14 year old. Hormones secreted from the pituitary gland situated in the brain stimulates eggs in the ovaries to grow, mature and to ovulate on a monthly basis.</p>
						<p>Female hormones secreted from the ovaries then thicken the endometrium (the lining of the uterus) cken in readiness for the implantation of the embryo.</p>
						<p>If the embryo does not implant, pregnancy does not occur and treated the endometrium is shed as a period. This is known as the menstrual cycle.</p>
						<p><strong>The risks related to menopause</strong></p>
						<p>After menopause, women’s risks of developing coronary heart disease hypertension, heart attack and stroke) increases to reach the same level as that of men. The incidence of osteoporosis (progressive&nbsp;bone&nbsp;disease&nbsp;that is characterized by a decrease in bone mass and density which can lead to an increased risk of&nbsp;fracture) also increases with menopause.</p>
						<p><strong>Types of menopause</strong></p>
						<p>There are three sorts of menopause</p>
						<ul>
							<li>Natural</li>
							<li>Premature</li>
							<li>Artificial</li>
						</ul>
						<p><strong>Natural menopause</strong>&nbsp;occurs when the levels of oestrogen and progesterone decrease until the menses stops completely as mentioned above.</p>
						<p><strong>Premature menopause</strong>&nbsp;happens when the periods stop before the age of 40 years. There are many causes for this like chromosomal defects, chemotherapy, radiotherapy, smoking, chemicals, pesticides and autoimmune diseases. Sometimes no cause can be found.</p>
						<p><strong>Artificial menopause</strong>&nbsp;can be a result of surgical removal of both ovaries or ovarian failure due to chemo or radiotherapy.</p>
						<p><strong>Symptoms and diagnosis</strong></p>
						<p>A change in the menstrual cycle is usually the first sign of menopause. Periods may typically be lighter shorter, the time between periods may increase and there may be several months of missed periods.</p>
						<p>Women may experience a variety of physical and psychological changes. For the majority of women, menopause is usually a gentle process in which women are able to cope. For some however, these symptoms may be so overwhelming that they require the help of gynaecologists. These symptoms are temporary and women may experience them for one to even up to five years.</p>
						<p><strong>Physical signs</strong></p>
						<ul>
							<li>Hot flushes</li>
							<li>Night Sweats</li>
							<li>Tiredness</li>
							<li>Headaches</li>
							<li>Joint and bone aches</li>
							<li>Palpitations</li>
							<li>Dry skin and sagging skin tone</li>
							<li>Vaginal dryness</li>
							<li>Urinary urgency</li>
							<li>More frequent urinary and vaginal infections</li>
						</ul>
						<p><strong>Psychological signs</strong></p>
						<ul>
							<li>Poor concentration</li>
							<li>Anxiety and depression</li>
							<li>Mood swings and irritability</li>
							<li>Insomnia</li>
						</ul>
						<p><strong>Possible treatments</strong></p>
						<p>The degree that these physical and psychological symptoms affect women varies from mild to severe.</p>
						<p><strong>Different treatment methods</strong></p>
						<p><strong>Hormone Replacement Therapy (HRT)</strong></p>
						<ul>
							<li>Conventional Hormone Replacement Therapy nowadays can be given when all other treatments have failed to relief hot flushes, night sweats and vaginal dryness. HRT can likewise help to decrease the risks of heart disease and osteoporosis.</li>
							<li>It is not advisable to be on conventional HRT for more than 5 years as this would then increase the risk of breast cancer.</li>
							<li>HRT can be given as oral tablets, topical skin patches and as vaginal preparations such as creams.</li>
							<li>Vaginal preparations can help to reduce symptoms of vaginal dryness, and to some extent bladder and vaginal infections.</li>
						</ul>
						<p><strong>Natural and alternative treatments</strong></p>
						<ul>
							<li>Healthy Lifestyle</li>
							<li>Evening Primrose oil, Remifemin, Livial (tibolone)</li>
							<li>Change in diet</li>
							<li>Exercise</li>
							<li>Calcium supplementation</li>
							<li>Weight management</li>
							<li>Stress reduction therapies</li>
						</ul>
