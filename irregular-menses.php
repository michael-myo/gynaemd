						<p>The Menstrual Cycle varies from woman to woman. It is normally counted from the first day of one period to the first day of the next. This menstrual bleeding can occur once every 21 to 35 days and can last between two to seven days. This is the norm for most women.</p>
						<p>Every woman usually has a certain cycle length that is rather constant (regular menses) but sometimes this can vary by one to two days every month. If the bleeding does not fit into this pattern, it is considered an irregular period.</p>
						<p>Some women who suffer from irregular periods have more frequent bleeding (polymenorrhea refers to cycles with intervals of less than 21 days) while others have less frequent periods (oligomenorrhea refers to irregular menstrual periods with intervals of more than 35 days).</p>
						<p>One of the more common causes of irregular menses is hormonal imbalances as female hormones determine the menstrual cycle. One must always exclude pregnancy as a cause of delayed menses. Post childbirth, miscarriage and breast feeding typically delays the return of menstruation. Sometimes uterine abnormalities like fibroids (non cancerous growth of the uterus) and endometrial polyps may cause heavy, more frequent and prolonged bleeding.</p>
						<p>Another common cause is polycystic ovarian syndrome (PCOS) associated with hormonal disturbances which affects women of reproductive age. The affected women commonly have enlarged ovaries with multiple small cysts and may be anovulatory (not ovulating). In addition, they may have menstrual cycles that are more than 35 days apart, have prolonged periods that may be heavy or scant or have four or more months without periods.</p>
						<p>Sometimes excessive weight loss as a result of eating disorders (anorexia nervosa) or extreme physical exercise can disrupt the normal menses pattern resulting in infrequent irregular periods. Pelvic inflammatory disease (PID) of the reproductive organs can also be a cause of irregular menses.</p>
						<p>Some women may also miss a period because of anxiety or stress. Stress causes the adrenal glands to increase secretions of cortisol which in turn, causes female hormone imbalances leading to irregular menses.</p>
						<p>Women who develop premature ovarian failure which is defined as a loss of ovarian function before 40 years old (either spontaneously or secondary to chemotherapy or surgery) usually start having irregular, infrequent menses before finally ceasing. Perimenopause (the period leading up to menopause) is another possible cause of irregular menses. Many women who are perimenopausal tend to experience irregular menses. Menopause is defined as one year of absent periods so if a post menopausal woman starts to experience irregular bleeding (post menopausal bleeding), then this should be thoroughly investigated to exclude more sinister causes like endometrial cancer (with a diagnostic hysteroscopy and curettage) or cervical cancer (with a pap smear and colposcopy).</p>
						<p>The treatment for irregular menses depends very much on the cause. If the cause is because of overwhelming stress, excessive exercise, extreme weight loss and eating disorders, then lifestyle changes are all that is required (reduce stress, moderate exercise and healthy diet).</p>
						<p>Pelvic inflammatory disease (PID) can be successfully treated with the appropriate antibiotics. Hormonal treatment in the form of contraceptives or non-contraceptive hormones is the treatment of choice for premature ovarian failure, hormonal imbalances and dysfunctional uterine bleeding (unknown cause). Uterine (fibroids, polyps) or ovarian (cysts) abnormalities can be treated initially with hormonal medication to regulate the menses and then, followed by surgical removal if medical treatment fails. For women who have irregular menses because they do not ovulate and are experiencing infertility as a result, they can be stimulated to ovulate with clomiphene citrate taken in the early part of the menstrual cycle. Once successful ovulation is achieved, they should have regular menses if they are not pregnant.</p>
