						<p>The vaginal ring offers protection against pregnancy without the inconvenience of barrier methods, spermicides or remembering to take a daily pill. The ring is a once a month combined hormonal contraceptive method which consists of a flexible transparent plastic ring. The ring contains hormones; estrogen and a progestogen, similar to the ones found in combined oral contraceptives (the pill). Women insert the ring into the vagina themselves which is easy to do. The ring is left in place for 3 consecutive weeks, the same number of days that is in one cycle of oral contraceptive pills. During this period it releases a steady low dose of hormones which prevent pregnancy by stopping the release of a mature egg (ovulation), thickens cervical mucus making it difficult for sperm to travel through the cervix and fertilise an egg if one is released and may alters the lining of the uterus so that a fertilised egg cannot implant into the uterine wall. &nbsp;After three weeks, the ring is removed to make way for a menstrual period, after a ring-free period of 1 week a new ring is inserted for another three weeks and so on. The week that the Ring is not worn, menstruation occurs.</p>
						<p>The Ring does not protect against any&nbsp;<a href="http://www.epigee.org/guide/stds.html" target="_blank" rel="noopener noreferrer">sexually transmitted diseases (STDs)</a>. First time users of the Ring will start this method of birth control during the first five days of their menstrual period. Because it takes seven days for the Ring to become effective, it will be necessary to use an additional form of birth control during the first week.&nbsp; For women already using another form of hormonal birth control, then the Ring will be effective immediately after the switch.</p>
						<p>Women like the convenience of only having to worry about their contraception once a month. It does not interfere with the spontaneous intercourse. The vaginal ring can causes lighter and shorter menses with fewer cramps. Other benefits include decreased PMS (premenstrual symptoms), decrease acne, some protection against pelvic inflammatory disease and etopic pregnancy and decreased risk of endometrial and ovarian cancers. These are similar to the combined oral contraceptive pills.</p>
						<p>As with all hormonal birth control methods, the common side effects associated with the use of vaginal contraceptive rings include increased vaginal discharge / irritation or yeast infection, weight gain, nausea, spotting or breakthrough bleeding, headaches, breast tenderness and mood swings. These are rare. Even rarer are more serious health complications like an increased risk of blood clots in the legs, lungs, heart and brain, stroke, heart attacks, gallbladder disease, a slightly increased risk of breast cancer and cervical cancer but these disappear once it is stopped. Vaginal ring users who smoke and who are obese are more at risk of these complications than other users. The ring and all hormonal contraceptive cannot be used if there is a history of breast cancer, poorly controlled diabetes, high blood pressure, high cholesterol, epilepsy, depression, gallbladder or kidney disease, recently had major surgery, sensitive vagina, prolapse uterus or bladder, rectal prolapse and breastfeeding. (Please note that all these side effects apply to hormonal contraceptives).</p>
						<p>Although it is not common, the Ring can slip out of the vagina. When used as directed, the ring is 95% and 99% effective, depending upon device usage. Once the contraception is stopped permanently, full fertility usually returns within a few months. The exact position of the ring in the vagina is not critical for its action. As long as it feels comfortable the ring is in the right position and will release the hormones necessary for&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_contraception.html">contraception</a>.</p>
