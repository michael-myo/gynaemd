						<p><strong>What causes breast cancer?</strong></p>
						<br>
						<p>The risk factors include family history (inherited mutated genes), personal history, increasing age, obesity, alcohol consumption, radiation exposure, early menarche and late menopause, having your first child after 35 years old, never been pregnant before and excessive hormonal exposure (women who take HRT for longer than 5 years post menopause)</p>
						<p><strong>Can anyone develop breast cancer?</strong></p>
						<p>Yes any woman is at risk of developing breast cancer but those with the above risk factors are at a higher risk.</p>
						<p><strong>What are the symptoms?/What should women look out for?</strong></p>
						<p>Breast lumps, skin changes (pitting and dimpling), change in shape of the breast, bloody nipple discharge, nipple inversion</p>
						<p>What forms of screening can be done to diagnose breast cancer?</p>
						<p>The most effective and proven method to detect breast cancer early is an X-ray of the breasts called mammogram although this can be combined with an ultrasound scan of the breast in some cases. Women are also encourage to do their own self breast examination on a monthly basis after their period has ended although this is still considered not as accurate.</p>
						<p><strong>How often should women get mammograms, and at what age should they start?</strong></p>
						<p>Women who are 40-49 should go for a screening mammogram once a year until they are 50 or when they reach menopause (after which screening mammograms are recommended every 2 years).</p>
						<p><strong>What steps, if any, can be taken to help prevent the development of breast&nbsp;cancer?<br></strong></p>
						<p>Maintain a healthy weight and lifestyle, avoid overexposure to long term hormonal therapy (women can go on HRT to relieve their menopausal symptoms as long as it is less than 5 years). Limit alcohol consumption. For some women with a very strong family history of breast cancer and tested positive for the breast cancer gene mutation, they may elect to undergo a prophylactic mastectomy. They may also choose to have their healthy ovaries removed (prophylactic oophorectomy) to reduce the risk of both breast cancer and ovarian cancer.</p>
						<p><strong>Are there any specific lifestyle changes that should be made to prevent&nbsp;this form of cancer from forming? (please provide any tips on nutrition,&nbsp;fitness, etc. if not mentioned above)</strong></p>
						<p>Aside from the advice above, there is no magic diet that has consistently shown to reduce the incidence of developing breast cancer.<strong><br></strong>
						<br>
						<strong>Can you tell me a little about the GynaeMD Pelvis, Breast and Bone package&nbsp;you offer to patients – what does it entail?</strong></p>
						<p>This is targeted for women 40 years and above or older women who may be menopausal as this screening package incorporates an ultrasound of the pelvis, measures bone mass density and includes a mammogram as well as blood cancer markers. Most <a href="https://www.gynaemd.com.sg/gynaecology_general.html" title="fibroids and cysts" style="color:#337ab7">fibroids and cysts</a> that are detected by the ultrasound scan can be left alone and observed if they are small as they are likely to resolve once menopause has occurred. Surgery may be necessary depending on their size, number, symptoms and likelihood of being cancerous. Similarly, breast lumps or cysts, if benign, can be observed, and if they appear suspicious, can be removed or referred to a breast surgeon for a second opinion. It is recommended that at the very least, a general screening is performed once a year for peace of mind, early prevention and detection of disease.</p>
					
