						<p>Many women delay visiting a gynaecologist out of embarrassment, fear or ignorance. While this is understandable, it is important to realise that, for the sake of good health, it is a must. And the process is not as daunting as it is thought to be.</p>
						<p><strong>When should a young woman go for her first gynaecologist visit?</strong></p>
						<p>Women should start seeing a gynaecologist if they have been sexually active for at least three years, or are sexually active for less than thar and are aged 21.</p>
						<p>After a woman has had sex, she needs a full gynaecological examination ar lease once every three years to track her reproductive health and review her contraceptive methods. Young, sexually-inactive girls with menstrual problems or puberty issues should also see a&nbsp;<a href="<?php echo homepage_url() ?>">gynaecologist</a>.</p>
						<p><strong>Why a gynaecologist and not a GP?</strong></p>
						<p>While pregnant women need co be under a gynaecologist’s care, any woman suffering from gynaecological problems or who would like a health screening on female issues should see a gynaecologist, a specialise who is better able co diagnose female problems properly.</p>
						<p><strong>How should one prepare for a visit to the gynaecologist?</strong></p>
						<p>Be clear and concise when describing your main concern, as your gynaecologist can only advise, investigate and treat based on specific informacion. Bring relevant medical reports, and come prepared with questions. When scheduling your appoinrment, bear in mind that if you want a pap smear, it cannot be done when you are menstruating.</p>
						<p><strong>What can one expect on the first visit?</strong></p>
						<p>Your gynaecologist will need details about your menstrual cycle, contraceptive use, allergies, previous operations and medical conditions, if you are on medication, and family health history. Your height weight and blood pressure will be recorded, and there may be a heart, lung and abdomen examination. A breast examination is usually performed. The pelvic examination involves an examination of the external genitalia, and a speculum is then gently inserted into the vagina to visualise the cervix in women who have already had intercourse. A pap smear is performed as part of the routine&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_cancer.html">cervical cancer screening</a>&nbsp;procedure. Iris relatively painless if you are relaxed. Finally, a pelvic ultrasound scan will reveal pelvic abnormalities such as fibroids and cysts.</p>
						<p><strong>What should a good gynaecologist do, ask or say?</strong></p>
						<p>A good gynaecologist will quickly make you feel comfortably relaxed, and will take the time to answer all your questions and address your concerns directly and sensitively without rushing you through. If he or she fulfils these criteria, then that’s the one for you. If not, find another gynaecologist more suited to your needs.</p>
						<p><strong>What are the most common gynaecological platforms among Singaporean women?</strong></p>
						<p>Depending on their age, women experience a range of issues about&nbsp;<a href="<?php echo homepage_url() ?>obstetrics_pregnancy.html">pregnancy</a>, menstruation,&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_contraception.html">contraception</a>, ovarian cysts, endometriosis, fibroids, infertility, female cancers and menopause.</p>
						<p><strong>There are so many gynaecological test we hear about. What are must-do-ones?</strong></p>
						<p>All sexually active women need a pap smear test performed within three years of commencing intercourse, and this must be repeated once every three years until age 65, provided there are no abnormalities. Mammogram, as the most effective and proven method to detect breast cancer, is the other must-do. Women aged 40-49 should go for a screening mammogram once a year until they are 50 or reach menopause, whichever is later, after which they should go every two years.</p>
