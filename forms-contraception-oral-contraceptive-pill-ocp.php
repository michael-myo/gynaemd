					<div class="page-content">
						<p>The Pill is a tablet containing two female-type hormones – an oestrogen and a progestogen. Various different oestrogens and progestogens are used in the many different types of Pill which are available. These two hormones stop ovulation (‘producing an egg’) each month. And if women don’t ovulate, they won’t get pregnant. The hormones thicken the secretions round the cervix – thus making it more difficult for sperm to get through. Also, they make the lining of the womb thinner, so that it is less receptive to an egg.</p>
						<p>It’s very effective which is why so many millions of women rely on it. If taken exactly as prescribed, then its effectiveness is likely to be almost 100 per cent. So the Pill is just about the most effective&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_contraception.html">method of contraception</a>&nbsp;there is, apart from&nbsp;<a href="http://www.netdoctor.co.uk/sex_relationships/facts/sterilisation_women.htm" target="_blank" rel="noopener noreferrer">sterilisation</a>.</p>
						<p>A pack contains 21 Pills and women take one every day for three weeks. At the end of those three weeks, there is ‘break’ for a week. During those seven days, the menses occurs. Stopping the Pill at the end of the pack brings on the period. After the week’s break, women start on the next packet. So it’s ‘three weeks on and one week off’ throughout the year. It is now the practice to take the first-ever Pill on the first day of the period. Women should then be protected immediately.</p>
						<p>The pill can have some serious side-effects, but these are not common. They may include:</p>
						<ul>
							<li>Raised blood pressure</li>
							<li>A very small number of women may develop a blood clot which can block a vein (venous thrombosis) or an artery (arterial thrombosis, heart attack or stroke)</li>
							<li>A small increase in risk of being diagnosed with breast cancer</li>
							<li>A small increase in the risk of cervical cancer if the pill is used continuously for more than five years</li>
						</ul>
						<p><strong>More recently in Jan 2008 an important article in The Lancet reported the protective effects of the Pill against ovarian cancer. The background risk of ovarian cancer is 1 in 70. It looked at about 23000 women with ovarian cancer and 87000 women without ovarian cancer from 21 countries. One third in each group had ever taken the Pill before in their life. The mean duration of being on the pill was 4.5 years. Overall, they found that the longer the women were on the Pill, the better their protection against ovarian cancer. If a woman were to take the Pill for 5 years, she reduced her risk of ovarian cancer by 1/3. If she took the Pill for 15 years, she reduced her risk of ovarian cancer by ½. The shorter the time of cessation from taking the Pill the greater the protection persisted. This reduction in risk persisted for more than 30 years even after they had stopped taking the Pill. Although taking OCP is associated with a small increase in the risk of developing breast cancer, this risk is much smaller and exists only while on the pill and just after stopping.&nbsp; Whereas for ovarian cancer, the protection persists for decades. Overall the benefits of the pill outweigh the risks. Worldwide, the Pill has already prevented 200 000 ovarian cancers and 100 000 deaths from the disease and with more than 100 million women taking the pill today, it is estimated that it will prevent &gt;30 000 ovarian cancers per year over the next few decades.</strong></p>
						<p>The pill may not be suitable for all women, but for most women the benefits of the pill outweigh the possible risks. The pill may be unsuitable if:</p>
						<ul>
							<li>women might already be pregnant</li>
							<li>Smoke and are over 35, or are over 35 and stopped smoking less than a year ago</li>
							<li>Are very overweight</li>
							<li>Take certain medicines</li>
							<li>Have had a previous thrombosis</li>
							<li>Have a heart abnormality, circulatory disease or high blood pressure</li>
							<li>Have very severe migraines or migraines with aura</li>
							<li>Have breast cancer now or within the last five years</li>
							<li>Have active liver or gall bladder disease</li>
							<li>Have diabetes with complications, or have had diabetes for more than 20 years</li>
						</ul>
						<p>Advantages:</p>
						<ul>
							<li>Doesn’t interrupt sex</li>
							<li>Usually makes menses regular, lighter and less painful</li>
							<li>May help with premenstrual symptoms</li>
							<li>Reduces the risk of cancer of the ovary, womb and colon</li>
							<li>Reduces acne in some women</li>
							<li>May protect against pelvic inflammatory disease</li>
							<li>May reduce the risk of fibroids, ovarian cysts and (non-cancerous) breast disease</li>
						</ul>
						<p>Disadvantages:</p>
						<p>Temporary side-effects at first may include:</p>
						<ul>
							<li>Headaches, nausea, breast tenderness and mood changes</li>
							<li>Breakthrough bleeding (unexpected bleeding on pill-taking days) and spotting</li>
						</ul>
