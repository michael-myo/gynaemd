												<p>Dysmenorrhoea is more commonly known as “menstrual cramps”. Most patients describe the condition as a dull, throbbing or cramping pain that arises from the lower abdomen. For most women, this occurs just before and during their menstrual periods. The intensity of the pain varies – some women may experience an annoying but tolerable ache. Others may experience pain so severe that it disrupts their daily social activities, school or work for a few days each month. The pain can radiate to the lower back and thighs. There may also be associated sweating, dizziness, nausea, vomiting and even loose stools.</p>

												<p>There are two kinds of dysmenorrhoea – primary and secondary. There is no known cause of primary dysmenorrhea. It may be a result of prostaglandins causing contractions of the womb, which lead to pain during menses as the womb tries to expel its lining. Severe contractions can also cause vasoconstriction of the blood vessels feeding the womb and again lead to pain.</p>

												<p>There are several causes of secondary dysmenorrhoea. Adenomyosis is a condition in which the lining of the womb (the endometrium) begins to grow into the muscular layer of the womb. This causes bleeding into the muscle walls during menses, resulting in pain. The womb can also enlarge resulting in heavy menses as well. In endometriosis, the endometrial tissues somehow implant themselves outside the womb (in the fallopian tubes, ovaries and pelvic tissues). During menses the blood in these areas causes irritation and pain. <a href="https://www.gynaemd.com.sg/gynaecology_general.html" title="Uterine fibroids">Uterine fibroids</a> are noncancerous growths in the wall of the womb and may rarely cause painful periods. For some women, the cervical opening is abnormally small and narrow (cervical stenosis) which impedes the flow of menstrual blood, leading to increasing back pressure in the womb and pain as a result.</p>

												<p>Occasionally, the diseases associated with menstrual cramps may have complications. For example, endometriosis can damage the fallopian tubes or cause adhesions to develop around the tubes, leading to tubal blockage and <a href="https://www.gynaemd.com.sg/gynaecology_infertility.html" title="fertility problems">fertility problems</a>. The damaged fallopian tubes then increase the risk of a fertilized egg implanting in the tube rather than in the womb, resulting in an ectopic pregnancy.</p>

												<p>There are various simple medications available. Nonsteroidal anti-inflammatory drugs (NSAIDs) may help to relieve painful menstrual cramps. For women who seek concurrent contraception as well, they can be treated with hormonal contraception in the form of oral pills (Yasmin, Yaz, Mercilon, Marvelon), injections (Depoprovera), skin patches (Evra), implants (Implanon), a vaginal ring (NuvaRing) and the intrauterine system (Mirena).</p>

												<p>If medication does not work – and the severe dysmenorrhoea is caused by fibroids, endometriosis or adenomyosis – then surgery is the next stage. In this case, the abnormal tissue (myomectomy in the case of fibroid and cystectomy in the case of endometriotic cyst) is removed and the normal anatomy is restored as much as possible during surgery. This can be performed via key-hole incisions (<a href="<?php echo homepage_url() ?>gynaecology_mis.html">laparoscopy</a>) or open operations (laparotomy). In some cases, the whole womb (hysterectomy) and ovaries (oophorectomy) may have to be removed if all other treatment modalities prove to be unsuccessful.</p>

												<p>Patients can attempt to prevent dysmenorrhoea by taking oral NSAID a few days before and during the entire menstrual period. Being on short or long term hormonal contraception may also be preventative, especially after surgery. In the case of severe adenomyosis and endometriosis, a three to six month course of gonadotropin-releasing hormone (GnRH) agonists (Lucrin) to prevent menses and to induce a temporary state of menopause may help reduce the risk of recurrence. In some cases, GnRH agonists may be given prior to surgery to reduce the size of fibroids and the extent of the endometriosis or adenomyosis so as to facilitate the ease of the operation. In addition, regular exercise and eating a healthy balanced diet with omega-3 fatty acids, magnesium and vitamins (B1, B6 and E) may be helpful. Relaxation techniques, like yoga, massage or meditation may also work for some women.</p>

												<p>Recurrence varies from patient to patient depending on the cause (fibroids, endometriosis and adenomyosis), severity, type of treatment and risk factors for dysmenorrhoea. These risk factors include positive family history, earlier age at menarche, heavy menses, long menstrual periods and smoking. The risk of recurrence is lower as the woman approaches menopause. Patients should monitor their symptoms with regards to the severity of the pain during the pre-treatment stage. This gives a good indication of the need for medical or surgical intervention and also for any recurrence of symptoms post-treatment which may require an alternative treatment.</p>

