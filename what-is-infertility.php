					<div class="page-content">
						<p><strong>What Is Infertility and what are the treatments of Male &amp; Female Infertility?</strong></p>
						<p>Overview</p>
						<ul>
							<li><a href="<?php echo homepage_url() ?>gynaecology_infertility.html">Infertility</a>&nbsp;is the inability to conceive after at least 1 year of having regular intercourse without using any contraceptive methods. Secondary infertility refers to couples who have been able to get pregnant at least once before, but now are experiencing some difficulties.</li>
							<li>Typically, 85% of couples are successful in getting pregnant within the first 12 months of trying.</li>
							<li>A woman is most fertile in her early 20s. The chance of a woman getting pregnant after the age of 35 decreases by 1% per year and this worsens after age 40.</li>
						</ul>
						<p><strong>Symptoms</strong></p>
						<ul type="disc">
							<li>As mentioned earlier the couple would experience some difficulty in getting pregnant after trying for a year or more without using any forms of contraception.</li>
						</ul>
						<ul type="disc">
							<li>In some cases, an infertile woman may have irregular or absent menstrual periods. While in men, , infertility symptoms may be hormonally related such as reduced libido or difficulties in ejaculation.&nbsp; He may also have small testicles or abnormalities in the scrotum.</li>
						</ul>
						<p><strong>Causes</strong></p>
						<ul type="disc">
							<li>Many factors can cause infertility. It may be due to problems in the man, woman or sometimes both. In some cases, there may also be no cause found.</li>
							<li>Female infertility is usually a result of blocked fallopian tubes (congenital or following infections and endometriosis), inability to ovulate (anovulation) or problems with implantation. It may be caused by medical disorders such as diabetes, thyroid abnormalities, clotting disorders, following cancer treatment, abnormalities of the uterus and cervix (abnormal anatomy, scarring, stenosis), abnormal growths (submucosal fibroids, polyps in the the uterus and cervix), obesity, and poor nutrition.</li>
							<li>The causes of male infertility may be a result of genital infections or scarring from sexually transmitted diseases, testicular trauma, medical problems (diabetes), varicoceles (prominent testicular veins), hormonal abnormalities and abnormal sperm quality and quantity.</li>
						</ul>
						<p><strong>Treatments for men</strong></p>
						<p>Treatments for infertility in men may include:</p>
						<ul>
							<li><strong>Treating infections-</strong>&nbsp;Antibiotic treatment may cure genital infections but the damage may be permanent.</li>
							<li><strong>Treatments for sexual dysfunction</strong>&nbsp;Prescription medication and counseling can improve erectile dysfunction or premature ejaculation.</li>
							<li><strong>Hormone treatments</strong>– hormonal treatment should only be used in very specific cases under direct doctor supervision as this can potential harm fertility if used wrongly.</li>
							<li><strong>Surgery</strong>– Surgery is recommended when the vas deferens may be obstructed, or may be absent as in Congenital Absence of the Vas Deferens. In cases where no sperms are present in the ejaculate, sperms may then be recovered directly from the testicles.
								<br> Surgery can also be used to remove varicoceles (abnormal veins) but only in selected cases.</li>
							<li><strong>Intrauterine insemination (IUI) and Assisted reproductive technology (ART)</strong>-Sperm is obtained directly through ejaculation, surgical extraction or from sperm donors, depending on individual circumstances. The sperm is then inserted into the uterine cavity (IUI), or used to fertilize the egg via&nbsp;<a href="http://ivf.gynaemd.com.sg/" target="_blank" rel="noopener noreferrer">in-vitro fertilization (IVF)</a>&nbsp;or intracytoplasmic sperm injection (ICSI).</li>
						</ul>
						<p><strong>Treatment for women</strong></p>
						<p>Before embarking on this, all women should be aware that as with all fertility treatments, they can not only improve the chances conceiving but also increase the likelihood of multiple pregnancies.</p>
						<ul>
							<li><strong>Stimulating ovulation with fertility drugs-</strong>&nbsp;Fertility drugs are recommended treatments for women who are infertile due to ovulation disorders. It increases the chances of ovulation and therefore pregnancy.</li>
							<li><strong>Intrauterine insemination (IUI)</strong>-During IUI, healthy sperm are collected and prepared and then placed directly in the uterine cavity around the time of ovulation (ovary releases one or more eggs to be fertilized).</li>
							<li><strong>In-vitro fertilization (IVF)</strong>&nbsp;or intracytoplasmic sperm injection (ICSI)- Follow a period of hormonal stimulation, the mature eggs are retrieve via a minor surgical procedure from the ovaries. These eggs are then fertilized with sperm (IVF) or via direct sperm injection (ICSI). A few days later, the viable embryos are then placed back into the uterine cavity.</li>
							<li><strong>Surgical procedures for infertility</strong>– These hysteroscopic surgeries can potentially improve pregnancy rates. The aim is to remove abnormal uterine septum, fibroids and endometrial polyps that may prevent successful implantation.</li>
						</ul>
