				    <p>Dr Ng sangat berpengalaman dalam bidang scan USG antenatal dan ginekologis, pemeriksaan pranatal, penilaian risiko dan perawatan antenatal, persalinan normal, persalinan menggunakan alat (vakum dan forsep) serta bedah sesar, kolposkopi, sistoskopi dan penanganan pasien dengan masalah menopause dan fertilitas. Selain bedah ginekologi umum, dia juga diakui dalam bedah laparoskopi canggih dan berminat pada bedah untuk inkontinensia kemih dan prolaps utero-vaginal. Dr Ng mempresentasikan banyak makalah berkaitan topik-topik tersebut di berbagai konferensi kedokteran di Singapura dan internasional, selain memenangkan hadiah kedua untuk risetnya pada International Scientific Meeting of the Royal College of Obstetricians and Gynaecologists ke-6 pada tahun 2005. Dia juga menulis berbagai makalah kesehatan, menjadi reviewer pada beberapa jurnal dengan reviewer sesama kolega dalam bidang obsgin (obstetri dan ginekologi) dan sangat dihormati di dunia riset kedokteran. Layanan DR Ng penuh dedikasi pada para pasiennya telah memberinya 4 kali Service from the Heart Awards dan Excellent Service Award (EXSA) nasional, sedangkan komitmennya pada kerjaan dan pasien diakui dengan Long Service Award dari KK Hospital pada tahun 2007.</p>
