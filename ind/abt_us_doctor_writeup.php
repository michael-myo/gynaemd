				    <p>Dr Christopher Ng menjalani pendidikannya di Singapura dan United Kingdom. Cita-cita menjadi dokter spesialis obsteri dan ginekologi berawal dari keluarganya yang terdiri dari para dokter ternama dalam dua generasi. Oleh itu, setelah lulus dari Marlborough College di England, Dr Ng melanjutkan studinya untuk mengambil gelar di bidang Kedokteran dan Bedah di St Mary’s Hospital Medical School di London pada tahun 1995. Pada tahun yang sama dia kembali ke Singapura dan menjadi Presiden dan Anggota Pendiri dari Asosiasi Alumni Kedokteran Luar Negeri Singapura (Overseas Medical Graduates Association of Singapore).</p>
