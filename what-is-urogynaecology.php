						<p><strong>What Is Urogynaecology?</strong></p>
						<p>Urogynaecology includes the diagnosis and treatment of urinary incontinence and female pelvic floor disorders (prolapses). Urinary incontinence and pelvic floor problems are surprisingly more common than what women perceive yet numerous ladies are hesitant to seek a gynaecologist’s get help because of the embarrassment associated with these conditions.</p>
						<p><strong>What are the common problems?</strong></p>
						<p>Women effected by uterovaginal prolapse (uterus, bladder or rectum) will usually complain of an uncomfortable lump which may in severe cases protrude out of the vagina. In&nbsp;<a href="<?php echo homepage_url() ?>gynaecology_urogynaecology.html">urinary incontinence</a>, there is an involuntary leakage of urine which is not only inconvenient but also highly embarrassing. Both these conditions may restrict a woman’s social activities.</p>
						<p><strong>What are Pelvic Floor Disorders</strong></p>
						<ul type="disc">
							<li>Pelvic floor prolapse occurs when the pelvic floor supports (ligaments and muscles) are damaged and weakened as a result of menopause, obesity, surgery, over strenuous exercises (weight lifting), prolong labour and traumatic vaginal delivery. These prolapses can involve the uterus (uterovaginal prolapse), bladder (cystocele) and rectum (rectocele).</li>
							<li>There are various types of urinary incontinence. The most common being stress urinary incontinence which is the involuntary leakage of urine when a woman strains (coughing, laughing, sneezing, running, jumping). The other common type being urge incontinence in which involuntary leakage of urine occurs before one is able to reach the toilet and this is often preceded by a sudden intense urge to urinate.</li>
						</ul>
						<p><strong>Methods of diagnosis</strong></p>
						<p>There are several different methods used to correctly diagnose these various problems so that the most effective treatment can be recommended.</p>
						<p><strong>Urodynamic Studies</strong></p>
						<p>This is the best method to differentiate the various types of urinary incontinence.</p>
						<ul>
							<li>In this process, a small catheter is placed into the patient’s bladder which gives accurate readings of the pressure in the bladder and urethra while it is being filled with water.</li>
							<li>The patient is also asked a series of questions that helps in providing a better understanding of the patient’s type of urinary incontinence and how it affects their daily activities.</li>
							<li>The amount of water that the bladder can hold is measured along with the volume at which the first urge to void is felt. The patient is asked to cough to determine the extent of stress incontinence if any. Involuntary bladder contractions are measured to see if there is any urge incontinence. Finally the patient urinates and the amount is measured to evaluate flow rates, amount voided and residual urine.</li>
							<li>The test is not painful and takes approximately 20 minutes.</li>
						</ul>
						<p><strong>Cystoscopy</strong></p>
						<ul>
							<li>Cystoscopy involves insertion of a cystoscope (which is a fine tube which has a camera attached to one end) into the bladder via the urethra in order to visualize the internal architecture of the bladder to exclude any anatomical abnormalities.</li>
						</ul>
						<p><strong>Urinalysis</strong></p>
						<ul>
							<li>This is simple procedure in which the patient’s urine is tested for any infections</li>
						</ul>
						<p><strong>Pelvic-muscle evaluation</strong></p>
						<ul type="disc">
							<li>To monitor and measure the patient’s ability to contract her pelvic muscles, a pelvic examination or an electronic monitoring device is used to subjectively or objectively measure this respectively.</li>
						</ul>
						<p><strong>Treating the disorder</strong></p>
						<p>In the majority of cases which are affected by mild to moderate symptoms, conservative management with or without medication is tried first.</p>
						<p>These include:</p>
						<ul type="disc">
							<li>prescription medication for urge incontinence</li>
							<li>pelvic muscle exercises</li>
							<li>change in lifestyle and dietary adjustments</li>
						</ul>
						<p>In the event that these conservative measures fail to provide sufficient relief or if the symptoms are severe in the first place, surgery may be recommended to repair pelvic floor prolapses or to treat urinary incontinence.</p>
						<ul type="disc">
							<li>tension free vaginal tape (TVT-O) obturator is very effective in treating urinary stress incontinence.</li>
							<li>various pelvic floor repair operations with or without a vaginal hysterectomy is the surgery of choice in the treatment of uterovaginal prolapse.</li>
						</ul>
